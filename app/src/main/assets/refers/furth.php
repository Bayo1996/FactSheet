	<h5><b>For further help</b></h5>
<p>If you’re a teenager and a parent, there are ways to help yourself and your child.</p>
<p> Getting support from your family, friends and services in the community can help you cope with being a young parent:</p>
<ul>
<li><p>See if there’s a way you can finish school if you haven’t already. This might mean studying at night while a partner, friend or parent minds your child.</p>
<p> In the long run, having an education will help with your chances of finding a job, and studying can help you to feel less lonely.</p></li>
<li><p>If you can stay with your parents while your child is young, this may help you deal with the pressures of caring for your child or coping financially.</p>
<p> Your parents might also be able to give you some support when you need it, and even share some tips from when you were a baby.</p></li>
<li>If you’re on your own or living away from your family, find out what sorts of financial support you can get .</li>
<li>Contact your local community centre about support groups for parents. These sorts of group can provide emotional support as well as information on child development and health care.</li>
<li><p>See if your local social worker  can put you in touch with a counseling service.</p><p> Counseling can help parents with their own issues as well as those associated with being a parent at a young age.</p></li>
<li><p>Speak to different experts that you come in contact with – such as your doctor, community health nurse or other experts associated with child care – to learn about creating the best home environment for your child.</p>
<p> They can also help you learn about topics such as nutrition, health and emotional development.</p></li></ul>
