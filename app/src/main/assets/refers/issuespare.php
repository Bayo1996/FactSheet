 <h5><b>Issues for teenage parents</b></h5>
<p>Being a parent can make it harder to get an education or find a job.</p>
<p> Juggling the responsibilities of being a parent with work, school and a social life can be tiring.</p>
<p>It can also be very difficult to get child care, and almost impossible to get affordable child care.</p>
<p> These difficulties can make it hard to cope financially, and can make parents feel lonely and apart from their family and friends.</p>

<p>On the other hand, teenage parents can find they have all the energy in the world to keep up with toddlers.</p>
<p> They can also be better than older parents at dealing with the new lifestyle of being a parent; they’re much better at dealing with little or no sleep, for example.</p>
<p> But young parents who are still physically developing while pregnant can struggle with sustaining two growing bodies at once.</p>

<p>Some teenage parents might feel they’re so busy trying to cope with the rest of their life that they can’t give their children enough attention.</p>
<p> Because they might also have to worry about finishing their education, working or finding a job, they might not feel as though they can enjoy their children or feel as satisfied as much as older parents.</p>

