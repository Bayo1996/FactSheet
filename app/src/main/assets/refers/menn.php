<h4><b>The CD4+ cell</b></h4> 
<p>The CD4+ cell count <b>measures the concentration</b> of a particular type of lymphocyte, the T-helper cell, in a specified amount of the patient’s blood. <b>The CD4+ cell is severely affected by HIV infection.</b></p>

<P>In the body, HIV attaches to and enters CD4+ cells. Once inside, the virus subverts the CD4+ cell’s replication machinery, turning it into a virus factory. In the process of copying itself, the virus destroys the CD4+ cell. 
Often when a patient begins antiretroviral therapy, the CD4+ <b>count will increase a reflection of the immune system’s improved ability to fight infection.</b> Determining a patient’s CD4+ cell count at regular intervals allows the health professional to monitor <b>the strength of the patient’s immune system.</b> Successful antiretroviral therapy will cause the CD4+ cell count to rise and then remain elevated during therapy. A decrease in the concentration of CD4+ cells may represent failure of antiretroviral therapy. </P> 
