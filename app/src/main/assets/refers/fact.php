<h4><b>ART</b></h4>

<p><b>Antiretroviral therapy (ART):</b> it is giving ARV drugs in the correct combination, with adherence support.</p>
<p> The powerful combination of three different antiretroviral drugs is called ART regimen.</p>
<p> This is the standard of good therapy and has the greatest benefit for the longest time.</p>
<p> ARV means “<b>Antiretroviral</b>” drugs.</p>
<p> ART means “<b>Antiretroviral therapy</b>”.</p>
<p> It is a combination of at least three ARV drugs however, on many occasions ARV and ART may be used interchangeably. </p>

<h4><b>Key Facts</b></h4>
<ol>
<li>Many antiretroviral drugs are available for the treatment of HIV infection. However, none of these medications can cure HIV.</li> 
<li>Medications used to treat HIV reduce the ability of the virus to replicate (or reproduce) within the body.</li>
<li>When HIV is unable to replicate, damage to the immune system and other vital organs is reduced, usually leading to improved health.</li>
<li>Patients receiving antiretroviral therapy are less susceptible to opportunistic infections, malignancies, and other illnesses.</li> 
<li>Many patients live longer as a consequence of antiretroviral therapy.</li> 
<li>One goal of antiretroviral therapy is to reduce the patient’s virus load, preferably to below levels that can be detected with available tests.</li> 
<li><p>There are three main groups of antiretroviral drugs:</p> <ul><li>nucleoside reverse transcriptase inhibitors (NRTIs)</li><li> non-nucleoside reverse transcriptase inhibitors (NNRTIs)</li><li> protease inhibitors (PIs).</li></ul><p> Each group of drugs works at a different point in the life cycle of the virus within the cell.</p></li> 
<li>The decision to begin antiretroviral therapy in an HIV-infected person is determined by that person’s clinical, virologic, and immunologic condition.</li> 
<li>Antiretroviral therapy often is changed when an HIV-infected child or adult experiences clinical, virologic, or immunologic failure.</li> 
<li>Among the numerous barriers to treatment are cost, time commitment, compliance, and home environment. </li>
</ol>
