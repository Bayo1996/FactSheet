<p style="font-size: 15px">Being active is good for you in lots of ways – it can help to build your muscles, keep your bones strong, burn fat and keep your heart healthy.</p>
 <p>Some people who are living with HIV experience <b>loss of muscle mass and strength</b>, so exercise can help prevent this.</p>
<p>You might get your exercise by <b>taking part in a sport, or going to the gym, but everyday activities like dancing,  walking and gardening</b> can all help get you moving.
If you want to do more exercise, feel fitter, have more stamina, more flexibility, or lose weight, there are three types of exercise to think about. These include:
<ol>
<li><b>Cardio or aerobic exercise</b> – <br>this increases your heart rate to help blood flow right around your body, delivering oxygen to your muscles and keeping your heart and lungs healthy. Try running, swimming, dancing or cycling</li>
<li><b>Resistance training</b> –<br> this increases the strength of your muscles by lifting weights for a period of time and then repeating. Even lifting a tin of beans will do!</li>
<li><b>Flexibility training </b>– <br>this stretches different parts of your body to strengthen your muscles and joints. Try stretching before and after any exercise you do, or focus on flexibility with an activity like yoga.</li>
</ol>
Particularly as we get older, regular exercise is important in reducing the risk of heart disease and diabetes, which can be more common in people living with HIV. Exercise is also great at reducing feelings of stress and symptoms of depression.</p>
<h4><b>Staying happy and fulfilling your potential</b></h4>
<p style="font-size: 15px">Taking care of yourself is not just about your physical health, but looking after your mental and emotional health too.</p>
<p>Finding out you have HIV can be a shock, and it may take you some time to adjust. Support from your friends and family, or other people living with HIV, can really help when you are finding things difficult.</p>
<p>Once you begin coming to terms with HIV, it’s a good idea to think about the rest of your life, i.e.</p>

<ul>
<li> <b>What are your goals?</b></li>
<li><b> What’s important to you?</b></li>
</ul>
<p> Maybe you want to study, have a family, travel or change career – HIV shouldn’t stop you doing any of these things!</p>
<h4><b>What else can I do to take care of myself?</b></h4>

<p> Many of the things we do to look after ourselves are common sense. Try to get plenty of rest and sleep.</p>
<p> <b style="color: red">If you smoke, try to stop</b> it isn’t always easy, but there is support available to help you.</p>
<p> If you are concerned about your alcohol or drug use , talk to a healthcare professional for advice and support.</p>
<p>It’s also important to think about any other health conditions you have; particularly as we get older, we’re more likely to experience other health problems.</p>
<h4><b>Exercising</b></h4>
<p style="font-size: 15px">Youths should be encouraged to do a lot of exercising.</p>
<p> They do not have to belong to any gym, but they can exercise at home as individuals or start their own club at their own time.</p> <p>Time can be set aside on some days for exercising during support group days, an hour will be enough.</p>
<p> When you exercise you also produce more blood cells, which means a better immune system.</p>
