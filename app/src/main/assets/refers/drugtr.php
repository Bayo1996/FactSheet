<p><b>Dancing</b> is a great way to get exercise without feeling it. Put on the radio or play a CD that the group members like.</p>
<p> When you dance you will also feel “happy” and your body will benefit from the extra oxygen it will receive.<p>
<p><b>Oxygen</b> also helps kill infections.</p>
