<h4><b>Suicide: What to do?</b></h4>
<p><b>How to manage a suicidal or self-harm client</b>
<ol>
<li><b>Evaluate whether the person has attempted a medically serious act of self-harm or suicide.</b>
<ul>
<li>Ask about past self-harm attempts.</li>
<li>Look for signs of poisoning, intoxication, and self-injury.</li>
<li>Closely monitor the person to prevent further self-harm</li>
<li>Do not leave the person alone or unsupervised.</li>
</ul></li>
<li><b>Evaluate if there is an imminent risk of self-harm or suicide.</b><ul>
<li>Ask about thoughts or plans to commit suicide or self-harm.</li>
<li>Look for signs of severe emotional distress, hopelessness, agitation, uncommunicative behaviour, or social isolation.</li>
</ul></li> 
<li><b>If risk of suicide or self-harm is imminent:</b><ul>
<li>Remove access to means of self-harm.</li> 
<li>Create secure, supportive environment, ensure person is not left alone.</li>
<li>Take client to a psychiatric hospital, w/ company (family, support person, or a health worker).</li></ul></li></ol> 
</p>

























