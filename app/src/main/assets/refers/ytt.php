  <p><b>Mental health:</b> psychological or emotional well-being of an individual. </p>
<p>Four main aspects of mental health.
<ul> 
  <li>An appropriate awareness of self</li> 
  <li>An awareness of one’s abilities </li>
  <li>An ability to work productively </li>
  <li>The ability to contribute to their communities</li>
</ul></p>
<p><b>Mental illness</b> is any disease or condition affecting the brain that significantly influences or disrupts a person's thinking, feeling, mood, ability to relate to others and daily functioning. It is persistent. </p>
<p>Temporary distress or changes in function in response to a stressful event is not considered a mental illness.</p>
<p>States between mental health and mental illness are broad.</p>
