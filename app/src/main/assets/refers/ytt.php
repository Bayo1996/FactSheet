   <li>One among the top ten causes of death worldwide.</li>
   <li>One of the 3 leading causes of death in the age group 15-35 years.</li>
   <li>90% may have some form of psychiatric disorder (Rich et al, 1986)</li>
  <li>History of suicidal attempts</li>
   <li>History of other deliberate self-harm</li>
