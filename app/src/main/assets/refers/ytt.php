<h4><b>Depression</b></h4>
<p><b>Symptoms: >2 weeks</b></p>
<ul>
<li>Depressed mood</li> 
<li>Loss of interest or pleasure</li>
<li>Weight loss</li>
<li>Low appetite</li>
<li>Insomnia or hypersomnia</li>
<li>Fatigue or loss of energy</li>
<li>Worthlessness or excessive guilt</li>
<li>Poor concentration or indecisiveness</li>
<li>Thoughts of death or suicidal ideations</li>
</ul>
<h4><b>Bipolar Disorder</b></h4>
<p><b>Mania:</b> 1 wk of elevated, expansive or irritable mood + 3 of the following:
<ul>
<li>Reduced need for sleep</li>
<li>Pressure of speech</li>
<li>Racing thoughts</li>
<li>Inflated self esteem</li> 
<li>Grandiosity</li>
<li>Increased goal directed activities</li>
<li>Excess involvement in pleasurable activities</li>
</ul></p>

