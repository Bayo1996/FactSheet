<p>Since the late 1980s, researchers have attempted to cure HIV infection. However, in the first two decades of the AIDS epidemic, such efforts were largely dangerous and unsuccessful.</p>
<p>HIV cure research to date has focused on clearing the virus from <b>T cells</b>, a type of <b>white blood cell</b> that is an essential part of the immune system.</p>
<P> Yet investigators have found the virus persists in HIV-infected <b>macrophages</b>.</P>
<P><b> Macrophages</b> are large white blood cells found in tissues throughout the body including the liver, lung, bone marrow and brain.</P>
<p> The discovery of this additional viral reservoir has significant implications for HIV cure research. </p>
<p>The journey toward a cure will not be easy and many challenges lie ahead. Some of the challenges are known, others may only become known as experiments proceed.</p>
<p> As with any great scientific endeavour, there will be <b>setbacks.</b></p>
<p> This means that research funding agencies and the public need to be patient while utilizing HIV Testing and Adherence to prevent HIV transmision as the journey towards cure goes on.</p>
<p> The initial wave of cure research experiments over the next five years should be viewed as exploratory and their results preliminary.</p>
<p> This research will seek to answer important scientific questions that can then be used to build a foundation as researchers work toward a cure.</p>
