<p>HIV can be transmitted through – and, as far as essentially all evidence shows, only through – several well-established routes:</p>
<ol>
<li>,By sharing injecting equipment</li>
<li>By receiving blood transfusions or other blood-related products from an infected person</li>
<li>From an HIV-positive mother to her baby</li>
<li>Through risky sexual activities.</li>
</ol>
    <p>What constitutes a <b>'risky'</b> activity (sexual or otherwise) is based on two kinds of data:</p>
    <p> theoretical (i.e., what is biologically plausible) and epidemiological (what has actually been seen in the <b>'real world'</b>).</p>
<p>Biologically, four conditions need to be present for transmission to occur:</p>
<ul>
<li>virus must be present in an infectious body fluid from the HIV-positive person</li>
<li> it must be present at sufficient levels to cause infection</li>
<li> there must be an effective route of transmission</li>
<li>it must reach susceptible cells in another person</li>
</ul>
<p>Well-established, evidence-based guidelines have classified sexual activities into risk categories based on whether these biological conditions for transmission are present, and on the number of new infections that can be attributed to these behaviours.</p>
<p> These risk categories are:</p>
<p><b>High risk:</b> well-established, predominant modes of transmission with clear biological and epidemiological evidence.</p>

<p><b>Low risk:</b> biologically plausible routes of transmission with relatively few attributable cases.</p>
<p> The possible ambiguities and uncertainties inherent in this category are discussed in detail below.</p>
<p><b>Theoretical risk:</b> biologically plausible routes with no clearly documented cases of transmission.</p>
<p><b>No risk:</b> 'impossible' modes of transmission with no documented cases or biological plausibility.</p>
<p>The relative risks of specific sexual activities, including anal, vaginal and oral intercourse and other forms of sexual activity, are summarised in the next section and discussed at length in following chapters.</p>
<p><b>Comparative risks</b> Comparing the precise degrees of risk of specific sexual activities comes with various caveats:</p>
<p>People rarely engage in one sexual behaviour to the exclusion of all others.</p>
<p><b>High-risk activities</b></p>
<ul>
 <li>Unprotected sexual intercourse: receptive or insertive, anal or vaginal.</li>
<li>Sharing unsterilised injecting equipment.</li>
<li>Being born to or breastfed by an untreated HIV-positive mother.</li>
<li>Receiving donated blood, organs or injections with unsterilised needles in countries with inadequate screening procedures.</li>
<li>Sharing uncleaned sex toys.</li>
</ul>
<p><b>Less risky activities</b></p>
<ul>
<li>Penetrative sex with appropriate barrier (<b>condom or female condom</b>)</li>
<li>Receptive oral sex with a man (giving a blow job).</li>

<li>Sharing injecting equipment if properly sterilised.</li>
</ul>

<p><b>Occupational risks</b> in invasive surgical and medical contexts.</p>
<p>Laboratory work with superconcentrates of HIV.</p>
