<p>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ECP can be used within 120 hrs. of sexual intercourse but the sooner the treatment, the more effective.</p>
<p>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ideally antiemetic’s should be offered with EC to reduce the chances of vomiting.</p>
<p><b>How EC works</b></p>

 Inhibition of transportation of the egg/sperm
 Prevention of ovulation<br>
