<p>If used consistently and correctly, male condoms are a very effective method of contraception.  
About two percent of partners who use condoms perfectly will get pregnant in the first year.   Under typical use (when they might be used inconsistently or incorrectly), </p> 
<p><b>STI Prevention:</b> Condoms are the only form of contraception that also provides protection against STIs. Male condoms are highly effective at preventing HIV and can help reduce the risk of other STIs as well.</p> 

<p><b>Female condoms</b>, also known as “internal condoms,” are soft, loose-fitting pouches with two flexible plastic rings on each end. They are put in the vagina to collect semen and block the sperm from entering the vagina.</p>

<p><b>Correct Use:</b> The female condom is inserted into the vagina with the closed side (inner ring) closest to the cervix and the open side (outer ring) left about 1 inch outside the vagina on the outer lips. 
It can be put in many hours before intercourse, but should not be left in for more than a total of eight hours. After intercourse, the female condom should be taken out immediately by gently squeezing and twisting the outer ring to keep semen inside the pouch.  This should be done before standing up.</p>  
<p>Female condoms can only be used once and should then be thrown away.</p> 
<p><b>Effectiveness:</b> If used consistently and correctly, female condoms are an effective method of contraception. About five percent of partners who use female condoms perfectly will get pregnant in the first year.   Under typical use (when they might be used inconsistently or incorrectly), about 21 percent of partners get pregnant in the first year of use. STI Prevention: If used correctly, the female condom can provide protection against STIs. Other barrier methods include diaphragms and cervical caps though these are less popular especially with young people.</p>
