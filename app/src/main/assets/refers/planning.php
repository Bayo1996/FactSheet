<p><b>CALENDER METHOD</b></p>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; How to identify the start and the end of the fertile period</li>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Before relying on this method, the woman records the number of days in each menstrual cycle for at least 6 months. The first day of the menstrual bleeding is always counted as day 1.
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Subtract 18 from the length of the shortest cycle. This tells u the estimated 1st day of the fertile time, then subtract 11 from the length of the longest cycle. This tells u the last day of your fertile time.
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;If record cycles vary from 26-32days: 
26-18=8(start abstinence on day 8)32-11=21(have sex after day 21)
