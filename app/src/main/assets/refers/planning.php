<br><p><b>I.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; COPPER IUCDs ( coil) </b></p>
It’s made of plastic with copper wire, and strings at the bottom.
It is inserted in a woman’s uterus by a health provider
It can protect a woman for up to 12 years but can be removed at any time if she wants to get pregnant
Insertion takes a few minutes, no operation or anesthetic required
A woman may feel some discomfort or cramps during insertion, or a few days after insertion 
Its 99.4% effective<br>
<b>Advantages;</b>
<li>Very cost effective- only 400-800KES for up to 12 years</li>
 <li>Very private- no one can tell a woman is using a contraceptive</li>
<li>No hormonal side effects</li>
<li>Very convenient- no daily action, or regular visits to the clinic</li>
<li>No delay in getting pregnant</li>
<li>Can be used by breastfeeding mothers and can be inserted immediately after birth</li>
<li>Effectiveness does not depend on users obedience</li>
<b>Disadvantages</b><br>
<li>Does not protect from STIs including HIV/AIDs</li>
<li>Must be inserted and removed by a trained heath provider</li>
<li>It is possible for the IUCD to come out especially if a woman does not have children or her uterus is small. A woman should check her pad during monthly bleeding, especially in the few months after insertion</li>
<li>Not good for some women i.e. women with ovarian cancer<br>
<b>IUCDS DO NOT;</b>
<li>Increase the risk of contracting STIs or HIV/AIDs</li>
<li>Move from the uterus to other parts of the body</li>
