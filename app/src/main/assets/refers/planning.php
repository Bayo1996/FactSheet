<li>Cause ectopic pregnancy</li>
<li>Prevent a woman from doing heavy work</li>
<li>Harm a man during sex, unless it is not well inserted or strings are not cut short enough.</li>

<p><b>II.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; CONDOMS</b></p>
<p>Male condom also known as “rubbers,” are worn over the penis to catch the sperm so they can’t enter the uterus and fallopian tubes.</p>  
<p>Most male condoms are made out of latex, a thin but strong rubber material. 
Condoms can also be made out of polyurethane. </p>
<img src="file:///android_asset/refer/images/cdss.jpg" style="width: 100%" alt=""/><br>
<p><b>Correct Use:</b> A condom is rolled onto the erect penis, leaving space at the tip for semen by squeezing the air out of the space. The male then withdraws the penis after intercourse, while it is still erect, holding the condom on the base of the penis, so it won’t slip off and spill sperm into the vagina. 
Condoms can only be used once and should then be thrown away. </p>

