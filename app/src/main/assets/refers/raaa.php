<h4><b>Partners & Spouses</b></h4>  
  <p>The issue of disclosing your status to your partner or significant other can be complex.</p>
  <p> If you've had unprotected sex with your partner, it's important to alert him or her to the fact that he or she may be at risk and should get tested. </p>
  <p>Regardless of your partner's decision to test and his or her results, by disclosing, you're making him or her aware of the need to practice safer sex together in the future.</p>
  <p>You might also want to tell your partner in an effort to get the emotional support you need.</p>
  <p> It's important to have someone to listen to your concerns, to offer suggestions, and to just simply be there.</p>
  <p> While some people fear becoming a burden when they have health problems, sharing your experiences can often be an opportunity for building a deeper intimacy and a stronger partnership.</p>
  <P>It's perfectly normal to experience anxiety about telling a partner.</P>
  <p> Before he or she can respond to your needs, your partner may first feel anxiety about his or her own HIV status (which can only be addressed through HIV testing) and may also feel angry and upset if the HIV infection occurred sexually, outside of the relationship.</p>
  <p> Disclosing your HIV status can add strain on the relationship so itâ€™s important for you to give some thought as to when and how to disclose.</p>
  <p> Depending on the nature of your relationship, you might want to consider some professional couples counseling.</P>
   <p>It's important for you to be aware of what the laws are in your state with regard to contact tracing and partner notification.</p> 
   <p>Contact tracing refers to the efforts of government agencies to identify any and all persons who might be at risk of contracting HIV from an infected person.</p>
   <p> Partner notification refers to information conveyed to spouses, sexual partners, needle sharers and others who might be at risk for HIV infection.</p>
   <p>
   <p> The laws regarding this vary from state to state. In many states, partner notification can be done anonymously through the state's Department of Health.</p>
   <p> The Department of Health in your state is a good source of information about what the legal procedure is in your state and how it might apply to you.</p>
 <h6><b>General contact tracing and partner notification issues to consider:</b></h6>
 <ul>
  <li>In general, physicians may breach patient confidentiality in this situation and notify a sexual or needle-sharing partner of an individual's HIV infection who is at identifiable risk of infection, as long as they act in good faith and offer only the information necessary to help the notified person protect against future risk of infection or seek medical treatment.</li>
  <li>These guidelines do not require that a physician disclose the name of the person who might be putting an individual at risk, although in some instances that identity will be obvious.</li>
  <li>These guidelines do not justify disclosures about HIV information to insurers, employers, schools or other institutions.</li>
  <li>Efforts at contact tracing need not breach medical confidentiality with regard to the HIV-positive person. They need only advise people that they may have been exposed to HIV.</li>
