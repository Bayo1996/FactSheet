<h4><b>HIV TESTING & COUNSELING(HTC)</b></h4>
 <p>HIV is diagnosed by doing a blood test.</p>
 <p>Therefore having a test is the entry point to receiving all HIV care and support</p>
  <h5>HIV has two types:</h5>
    <ol>
    <li>HIV type 1</li>
    -&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;First identified in 1983 
    -&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Responsible for most HIV infections globally
   <h5>Three groups</h5>
   <ol>
   <li>Group M has 9 subtypes (A,B,C,D,F,G,H,J,)</li>
   <li>Group O – largely in central Africa</li>
   <li>Group N</li>
   </ol>
   -&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Recombinant viruses
  <li>HIV type 2</li>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;First identified in 1985
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;West Africa 
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Limited spread to other countries
</ol>
<h4><b>A WORD ON TESTING</b></h4>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;HIV testing – widely available
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Testing should always be preceded by pre-test counseling and followed by posttest counseling
 <h5>Life changing effects:</h5>
 <li>Medical</li>
 <li>Psychological stress</li>
 <li>Social- discrimination, loss of a job or family</li>
 <h4><b>Available Test</b></h4>
<h5>-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Antibody tests</h5>
<li>Rapid tests</li>
<li>Noninvasive Rapid tests</li>
<li>Elisa </li>
 <h5>-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tests that detect the viral elements</h5>
<li>Western blot</li>
<li>P24 antigen</li>
<li>PCR</li>
-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Selection of a test depends on why the test is done