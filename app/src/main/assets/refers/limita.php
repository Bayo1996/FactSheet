<h4><b>ARE THERE LIMITATIONS WITH THE VIRAL LOAD TEST?</b></h4>
<ul>
  <li>Only about 2% of the HIV in your body is in the blood. The viral load test does not measure how much HIV is in body tissues like the lymph nodes, spleen, or brain. HIV levels in lymph tissue and semen go down when blood levels go down, but not at the same time or the same rate.</li>
  <li>The viral load test results can be thrown off if your body is fighting an infection, or if you have just received an immunization (like a flu shot). You should not have blood taken for a viral load test within four weeks of any infection or immunization.</li>
