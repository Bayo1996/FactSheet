<b>There is strong scientific evidence that circumcised men are less vulnerable to HIV infection via <b>heterosexual intercourse</b> than uncircumcised men.</b>
<p>Ecological studies from the 1990s had noted that countries with high rates of circumcision tended to have lower-than-average HIV prevalence for their region, and countries with low rates had high HIV prevalence.</p>
<p> These differences also extended to different ethnic populations within countries.</p>

<p>Laboratory studies have found that the inner surface of the foreskin has a very dense population of Langerhans cells, a type of cell that transports HIV from the skin surface into the bloodstream.</p> 
<p>The area under the foreskin can also trap bacteria acquired during sex and increase the risk of <b>sexually transmitted infections</b> (STIs).</p>
<p>Three <b>randomised controlled trials</b> (RCTs) of immediate versus delayed circumcision in men in three different locations in Africa reported their findings in 2005 and 2007.</p>
	<p>They found that circumcision cut the chances of heterosexual men acquiring HIV by between 50% and 75%.</p>
<p>Whether circumcising men with HIV protects women from infection is less clear.</p>
<p> There is possibly some direct protective effect, though this may be weakened by behaviour change and also by sex being resumed too early after circumcision.</p>
<p> However, mathematical models show that circumcising men should start to produce smaller, but significant, ongoing declines in female HIV prevalence too, as prevalence in men declines.</p>
<p>Evidence for any protective effect in gay men is even weaker, although there is some indication that circumcision may have some protective effect for men who only take the insertive role in sex.</p>
<p>There is stronger evidence that circumcision can protect heterosexual men against some other STIs, including herpes and the <b>human papillomavirus</b> (HPV), although it appears not to have any protective effect against <b>urethral sexually transmitted infections</b> (STIs), such as gonorrhoea and chlamydia.</p>
<p> Circumcision may offer gay men some protection against syphilis.</p>
<p>Mathematical models show that increasing the number of men circumcised in African countries could have a significant effect on HIV prevalence, although this may be more modest and act more slowly than some other measures such as the universal provision of HIV treatment.</p>
<p>Following these findings, the<b> World Health Organization</b> (WHO) recommended the national roll-out programmes of circumcision in high-prevalence African countries.</p>
<p> Progress in rolling out circumcision has varied from country to country.</p>
