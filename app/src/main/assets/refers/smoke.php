<p>Smoking is one of the most important causes of illness and death in HIV-positive people.</p>
<p>Smoking increases the risk of <b>lung cancer, heart disease, high blood pressure and stroke</b>.</p>
<p>HIV-positive smokers have a greater risk of lung cancer than other smokers.</p>
<p>Tobacco is a legal, and widely used, drug.</p>
<h4><b>Smoking and HIV</b></h4>
<p>Smoking, in itself, does not make HIV infection worse.</p>
<p>The rate at which <b>HIV disease progresses or the number of CD4 cells</b> lost is no greater in smokers than non-smokers.</p>
<p>However, there are higher levels of smoking among people with HIV than in the general population, with the accompanying health risks.</p>
<p> For people taking HIV treatment who have an undetectable viral load, smoking has a much greater impact on life expectancy than HIV infection.</p>
<p>It’s well known that smoking increases the <b>risk of heart disease , high blood pressure , and
stroke</b>.</p>
<p>There is evidence that smoking is the main risk factor for heart disease in people with HIV.</p>
<p>In addition, there is some evidence that having HIV may itself increase the risk of heart disease.</p>
<p>It’s well established that smoking increases the risk of a number of cancers, including lung cancer.</p>
<p><b>Stopping smoking</b> (or not starting in the first place) will significantly reduce your risk of developing heart disease and other cardiovascular illnesses.</p>
<p> Giving up, whatever age you are, can improve your day-to-day health as well as potentially increasing your life expectancy.</p>
<p>You are most likely to stop smoking and stay stopped if you are motivated.</p>
<p>More recently, <b>electronic cigarettes (e-cigarettes)</b> have become a popular aid for stopping smoking.</p>
<p>Giving up smoking is <b>hard</b>.</p>
<p> You are most likely to give up if you get expert support.</p>
<p> But there are things you can do, alongside this, to help you with it.</p>
<p> Avoiding ‘triggers’ – activities that you associate with smoking – where possible, may help. </p>
<p>Exercise can also be helpful.</p>
<p> Some people find that alternative therapies such as acupuncture and hypnotherapy help them stop smoking.</p>
