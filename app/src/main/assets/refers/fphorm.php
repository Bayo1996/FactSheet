<h4><b>HORMONAL METHODS</b></h4>
<P>Oral contraceptive pills, also known as “birth control pills” or “the pill,” are a kind of medication made up of hormones that keep a woman’s ovaries from releasing eggs as long as she keeps taking them. <b> They also thicken cervical fluid and thin the lining of the uterus to keep the sperm from joining the egg.</b></P><br> 

<li><b> Correct Use:</b> There are many different brands of oral contraceptive pills depending on your area — most come in either 21-day or 28-day packages.  Both types have 21 days of active pills; 28 day packs also have seven reminder pills.</li>  
<li>Women using a 21-day pack take one pill every day for three weeks in a row, wait a week, and then start a new pack; they will likely begin menstruating (get their period) during the week between packs.  Women using a 28-day pack  take  one pill every day and  start  a new pack  as soon as they finish; they will likely begin menstruating (get their period) during the last week of pills. </li>
<li><b>Effectiveness:</b> If used consistently and correctly (a woman never misses a pill), oral contraceptive pills are a very effective method of contraception.  Less than one percent of partners who use the pill perfectly will get pregnant in the first year.   Under typical use (when they might be used inconsistently or incorrectly), about eight percent of partners get pregnant in the first year of use.</li><br> 
<p><b>STI Prevention: Oral contraceptive pills provide no protection against STIs.</b></p><br> 
<p><b>INJECTABLES,</b> also known as “the shot,” are an injection of hormones that is given into a woman’s arm, hip, thigh, or buttocks either every month or every three months depending on the type.  The hormones keep a woman’s ovaries from releasing eggs and thicken the cervical fluid to keep the sperm from joining with the egg. There are two types of injectables contraception available, a progestin-only shot that is given every three months and a monthly injectables that contains both estrogen and progestin </p><br>
<li><b> Correct Use:</b> A woman receives an injection every four–12 weeks. In many areas, women can only receive the shot from a health care provider; however, there are some programs that allow community-based access agents, including YPPs, to provide the shot.</li> 
<li><b>Effectiveness:</b> If used consistently and correctly (a woman always gets her shot on time), injectables are a very effective method of contraception.  Less than one percent of partners who use injectables perfectly will get pregnant in the first year.   Under typical use (when they might be used inconsistently or incorrectly), about three percent of partners get pregnant in the first year of use. 
STI Prevention: Injectables provide no protection against STIs.</li> 
<p>Other hormonal methods include a CONTRACEPTIVE PATCH (which is worn on the skin), a contraceptive ring (which is placed inside the vagina), and implants (which are placed under the skin inside a women’s inner arm by a health care provider).</p><br>

<p><b>EMERGENCY CONTRACEPTIVE or “EC”</b> pills are oral contraceptive pills that can prevent pregnancy if taken after unprotected intercourse.</p> <br> 
<p><b>EC pills are not “abortion pills”;</b> they will not work if a woman is already pregnant.  In some areas prepackaged EC pills are available while in others they are not legal. 
If EC is not available, a woman can use <b>regular oral contraceptive pills for this purpose,</b> however, she must follow exact dosing instructions.<br> 
EC pills should not be used as a form of ongoing contraception because there are other forms of contraception that are much more effective and have fewer side effects.<br> 
<li><b> Correct Use:</b> The pills should be taken within 120 hours (five days) after unprotected intercourse, but the sooner that a woman takes the pills, the better chance she has of preventing a pregnancy. </li>
<li><b> Effectiveness:</b> If the pills are taken within 120 hours they can be 75–89 percent effective.</li> 
<li><b>STI Prevention:</b> EC pills provide no protection against STIs. </li><br> 
