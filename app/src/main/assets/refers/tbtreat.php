<p>TB is a treatable and curable disease.</p>
<p> Active, drug-susceptible TB disease is treated with a standard 6 month course of 4 antimicrobial drugs that are provided with information, supervision and support to the patient by a health worker or trained volunteer.</p>
<p> Without such support, treatment adherence can be difficult and the disease can spread.</p>
<p> The vast majority of TB cases can be cured when medicines are provided and taken properly.</p>.
<h4><b>Multidrug-resistant TB</b></h4>
<p><b>Anti-TB medicines</b> have been used for decades and strains that are resistant to 1 or more of the medicines have been documented in every country surveyed.</p>
<p> Drug resistance emerges when anti-TB medicines are used inappropriately, through incorrect prescription by health care providers, poor quality drugs, and patients stopping treatment prematurely.</p>
<p><b>Multidrug-resistant tuberculosis</b> (<b>MDR-TB</b>) is a form of TB caused by bacteria that do not respond to isoniazid and rifampicin, the 2 most powerful, first-line anti-TB drugs.</p>
<p> MDR-TB is treatable and curable by using second-line drugs.</p>
<p> However, second-line treatment options are limited and require extensive chemotherapy (up to 2 years of treatment) with medicines that are expensive and toxic.</p>
<p>In some cases, more severe drug resistance can develop.</p>
<p><b> Extensively drug-resistant TB</b> (<b>XDR-TB</b>) is a more serious form of MDR-TB caused by bacteria that do not respond to the most effective second-line anti-TB drugs, often leaving patients without any further treatment options.</p>
<p>Worldwide, only 54% of MDR-TB patients and 30% of XDR-TB are currently successfully treated.</p>
<p> In 2016, WHO approved the use of a short, standardised regimen for MDR-TB patients who do not have strains that are resistant to second-line TB medicines.</p>
<p> This regimen takes 9â€“12 months and is much less expensive than the conventional treatment for MDR-TB, which can take up to 2 years.</p>
<p> Patients with XDR-TB or resistance to second-line anti-TB drugs cannot use this regimen, however, and need to be put on longer MDR-TB regimens to which 1 of the new drugs (bedquiline and delamanid) may be added.</p>

