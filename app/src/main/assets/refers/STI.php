<P>1 in 2 sexually active people will get a sexually transmitted disease or infection – an STD/STI – by the age of 25 (and there is still risk beyond that).</P>
<p>But the good news is that they are avoidable, manageable and definitely.</p>

<h4><b>REMEMBER</b></h4>
<p>If you test positive for any STI, you should inform all recent sexual partners (past 60 days) so they can get tested and treated too.</p>

<h4><b>Can STDs be passed on to a baby?</b></h4>

<p>During early prenatal care, most women undergo tests to determine whether or not they have any STDs.</p>
<p> Some STDs can be transmitted to the fetus during pregnancy and others may be transmitted during the birth process if a woman has a STD at that time.</p>
<p>If you have intercourse with more than one partner during pregnancy, it is important to use latex condoms every time. If you are pregnant and know or suspect that a partner is having sex with others, talk with your doctor about your risks for STDs and how to reduce the chances of passing them to your baby.</p>

<h4><b>Can STDs be transferred through oral sex?</b></h4>
<p>They sure can. Although oral sex is often considered “safer” than vaginal or anal intercourse, it is still possible for STDs to be transmitted.</p>
<p>You can make oral sex even safer by using a latex condom or dental dam, which is a latex rectangle that can be placed between the mouth and the vagina, vulva, or anus. To view a full STD list and discover how each can be transmitted, take a peek at our Uncovering STDs tool.</p>

<h4><b>Can I get an STD from a toilet seat?</b></h4>

<p>No. STDs/STIs are transmitted from one infected person to another during vaginal, anal or oral sex or through intimate sexual contact (i.e., hand jobs, genital-to-genital contact without penetration, etc.)</p>
<p> The only other way that STDs can be transmitted is from an infected pregnant woman to her fetus or baby during childbirth.</p>
<p> Some infections, such as Hepatitis B and HIV are sexually transmitted but can also be transmitted from an infected person to another through direct blood-to-blood contact (i.e. sharing an intravenous needle).</p>
<p> To learn more about specific STDs and how they are spread, check out our Uncovering STDs tool.</p>

<h4><b>How soon can I be tested after having unprotected sex?</b></h4>

<p>If you think you’ve been exposed to an STD, go to your health care provider or a local health center to get tested.</p>
<p> Sometimes symptoms of an STD will show up just a couple of days after becoming infected, and sometimes it can take months.</p>
<p> If you think you may have been exposed, it is very important to not engage in sexual activity until you are tested and treated, if necessary. Remember, many infections do not cause any symptoms but they can still be transmitted to another person during sex.</p>
<p> If you believe you may be at risk for having an infection, get tested.</p>

<h4><b>What should I do if I think I have an STD?</b></h4>

<p>First off, don’t panic. You should go see a medical provider and get tested right away.</p>
<p> It’s also a good idea to hold off on having sexual contact with anyone until you know for sure what is going on and whether or not you have a STD. You can find a health center location in your neck of the woods by visiting our Health Center Locator.</p>

<p>People with multiple sexual partners, those who think they may have been exposed, those who’ve had unprotected sex with a partner whose health status was unknown, or anyone who has symptoms of an STD should definitely get tested. It’s the first crucial step on the road to proper treatment.</p>

<h4><b>Which STDs are curable?</b></h4>

<p>There are approximately 30 STDs that have been identified throughout the world.</p>
<p> Some can be cured with prescription medication and others remain in the body and are currently considered not curable, including HIV.</p>
<p> All STDs can be treated, meaning that any uncomfortable symptoms, such as herpes sores, can usually be helped through medication or certain procedures.</p>
<p> To see a full list of STDs and learn how each is treated, pull back the covers on our Uncovering STDs tool.</p>

<h4><b>Is every STD testable?</b></h4>

<p>Yes. There are tests available for all STDs, except for HPV in men. There are different tests for different STDs, but most are simple and non-invasive. The tests can range from blood or urine samples to vaginal swabs or small samples taken from the infected site (a sore, for example).</p><p> Find a full STD list and all the test details in our Uncovering STDs tool.</p>

<p>Visit a health center and talk with a health care provider to determine which infections are common in your area and which tests may be needed for your specific situation.</p>

<h4><b>How do I know if I have an STD?</b></h4>
<p>Quick question, quick answer. The only way to know for sure if you have an STD is to visit a health care provider and get tested.</p>
<p> If you believe you may have been exposed or contracted an STD, you can learn about the symptoms, treatment and other details in our Uncovering STDs tool.</p>
<p> Knowing that info is helpful, but to know for sure go ahead and get tested at a friendly site – just visit our convenient Health Center Locator.</p>

<h4><b>What is an STD vs. an STI?</b></h4>

<p>The difference, really, is in the terminology itself. Way back in the day, sexually transmitted infections were called “venereal diseases,” believed to have been so-called after the Goddess of Love, Venus.</p>

<p>Today, professionals use the terms <b>STD (sexually transmitted disease) or STI (sexually transmitted infection)</b> to discuss infections that are transmitted from one infected person to another through vaginal, anal or oral sex or through close intimate sexual contact.</p>
<p> Using either STD or STI is accurate, however more and more “STI” is being used as the most up-to-date term. The reason for this is that people can have an infection without it actually turning into a disease. Check out our Uncovering STDs tool to see a master list of STDs and learn how they are transmitted.</p>

<h4><b>What’s the best way to prevent spreading or getting an STD/STI?</b></h4>

<p>As with all STDs/STIs, the most effective protection is to abstain from sexual activity or be monogamous with one-long-term partner who has tested negative.</p>
<p> Using latex condoms (male or female) or dental dams can significantly reduce the risk of contracting or spreading an infection.</p>
<p> Using a water-based lubricant is A-OK, but don’t use anything with oils in it because it can make the condom ineffective. There are some STDs, like herpes and HPV that condoms are less effective at preventing (good reminder for guys and gals to get the HPV vaccine).</p>










