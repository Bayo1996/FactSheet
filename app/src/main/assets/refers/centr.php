<h4><b>Women’s Rights Awareness Programme (WRAP)</b></h4>
<h5><b>Resource Focus:</b></h5>
<p> Violated women and children</p>
<h5><b>What they provide:</b></h5>
 <p>Started in 1994, WRAP caters for the needs of abused and violated women and children in Kenya.</p>
 <p> The organization later expanded to also cater for unaccompanied minors who have been violated.</p>
 <p> They provide alternative accommodation and related supportive services like legal advice, medical care, trauma counseling, marital counseling, mediation, reconciliation, resettlement, and re-integration.</p>
 <p><b>WRAP</b> also undertakes to educate communities on causes and effects of gender violence and child abuse.</p>
