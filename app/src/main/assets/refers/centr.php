<h4><b>Gender-based Violence Recovery Centre- Coast Province General Hospital</b></h4>
<h5><p>Resource Focus:</p></h5>
<p> Survivors of rape</p>
<h5><b>What they provide:</b></h5>
<p> It was set up in 2007 as a result of a public-private partnership between the Ministry of Health and International Centre for Reproductive Health.</p>
