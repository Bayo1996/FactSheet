<h4><b>Centre for Assault Recovery of Eldoret (CAR-E)</b></h4>
<h5><p>Resource Focus:</p></h5> 
<p>Victims of sexual abuse</p>
<h5><b>What they provide:</b></h5>
<p> CAR-E was established by the Moi Teaching and Referral Hospital in partnership with other bodies such AMPATH, Kenya Police, German Development Cooperation and the Legal Aid Centre of Eldoret.</p>
<p> The Centre was established in May 2007 after a growing increase in the number of survivors of sexual abuse at the Accidents and Emergency department.</p>
<p> It aims to offer a safe, kind and respectful environment for diagnosis, treatment, and prevention of sexual and gender-based violence.</p>
<p>Victims can receive inpatient and outpatient services at the facility 24/7.</p>
<p> The victim can also get temporary shelter until they are physically and psychologically fit to be on their own.</p>
<p> Upon discharge, they do follow-ups for six months and also do have support groups.</p>
<p>Location:<br> <b>Moi Teaching and Referral Hospital</b>, Eldoret</p>
