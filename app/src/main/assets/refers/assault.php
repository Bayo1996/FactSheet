<p>If you don’t consent to sex and someone forces you to do something sexual, this is sexual assault, abuse, and/or rape.</p>
<p> Someone hurting you like this is never your fault.</p>
<p><b>What are sexual assault, abuse, and rape?</b></p>
<ul>
<li><p>Sexual assault or abuse means any unwanted sexual contact. It’s when someone uses force or pressure (either physical or emotional) to get you to do something sexual.</p> <p>Rape is when someone forces or pressures you into having sex.</p></li>
<li><p>Every state defines crimes like <b>"rape,” "sexual assault," and “sexual abuse” </b>differently.</p>
<p> Rape usually means forced vaginal, anal, or oral penetration by a body part or object.</p></li></ul>

<p><b>Here are some examples of things that are sexual abuse or assault:</b></p>
<ul><li>  someone touching your breasts, butt, or private parts without your consent</li>
<li>  someone showing you their genitals or making you touch them without your consent</li>
<li>someone rubbing their genitals against you without your consent (this sometimes happens in crowded areas like subways or buses)</li>
someone forcing you to kiss them
<li>  someone holding you down and kissing, touching, or rubbing against you</li>
<li>someone smacking your butt without your consent</li>
</ul>
<p>It doesn’t matter if the person doing those things is a family member, your friend, or even someone you’re dating — it’s still wrong.</p>
<p> Everyone has the right to decide what happens to their own bodies.</p>
<p> If you’re dating someone who doesn’t <b>respect your boundaries, you may be in an unhealthy or abusive relationship.</b></p>
<p>If one of your teachers, a family member, your boss at work, or anyone much older than you does anything sexual with you, that’s sexual abuse — even if you agree to it.</p>
<p> If a teacher or other adult in your life tries to touch or kiss you sexually or asks you to do anything sexual to them, tell another adult you trust as soon as possible.</p>
<p> Often, abusers will make you feel like you’ll be in trouble if you don’t do what they want or if you tell anybody.</p>
<p>  But what they’re doing is NOT okay — and no matter what you’ve said or done, it’s 100% their fault, not yours.</p>
