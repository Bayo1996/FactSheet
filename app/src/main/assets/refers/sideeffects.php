<p>You do not have to put up with <b>side-effects</b> – there is nearly always something that can be done about them.</p> 
<p>Talk to your doctor if side-effects are affecting the quality of your life.</p>
<p>It is an accepted reason for the need to think about changing the treatment you are on.</p>
<ol>
<li><b>Diarrhoea</b></li>
<p>Diarrhoea is a possible side-effect of most anti-HIV drugs.</p>
<p>With some drugs, diarrhoea goes away after the first few weeks or months of treatment.</p> 
<p>However, for some people it becomes a permanent feature of taking a particular drug.</p>
<p>Drinks with caffeine, such as coffee, and fatty and spicy food can make diarrhoea worse.</p> <p>It is important that you continue to eat and drink even if you have diarrhoea.</p>
<li><b>Feeling or being sick</b></li>
<p>Feeling sick (<b>nausea</b>) or being sick (<b>vomiting</b>) are possible side-effects of most anti-HIV drugs.</p>
<p> Your doctor may give you pills (called <b>anti-emetics</b>) to stop you being sick if you are starting an anti-HIV drug that has nausea and vomiting as a possible side-effect.</p>
<p> Taking these about 30 minutes before you take your anti-HIV drugs can help prevent these symptoms.</p>
<li><b>Headache</b></li>
<p>Headache is a possible side-effect of many anti-HIV drugs.</p>
<p> In most cases, these are mild and lessen or go away completely after a few weeks of treatment with the drug. Painkillers such as paracetamol can provide relief.</p>
<p>It makes good sense to tell your doctor about headaches, particularly if they are severe or last more than a few days.</p>
<li><b>Mood and sleep problems</b></li>
<p><b>Efavirenz</b> (Sustiva , also in the combination pill,
Atripla ), can cause a number of mood and sleep problems, including <b>anxiety and depression</b>, feeling spaced out during the day or having vivid dreams at night.</p>
<p><b> Rilpivirine</b> (Edurant ) can also cause mood changes and depression.</p>
<p>When these side-effects occur, they are normally mild and lessen or go away after a few weeks of treatment.</p>
<p>Tell your doctor if you are experiencing these side-effects.</p>
<li><b>Rash</b></li>
<p>Rash can be a side-effect of a number of anti-HIV drugs.</p>
<p>Such rashes often lessen or go away after a few weeks of treatment.</p>
<p> Even so it is a good idea to report rashes to your doctor.</p>
<p> A rash can also be a sign of an infection. Some rashes can indicate the presence of an allergic reaction.</p>
<li><b>Sexual problems</b></li>
<p>Sexual problems (sometimes called <b>sexual dysfunction</b>) are common in people with HIV.</p> <p>They are sometimes thought of as a side-effect of HIV treatment, but can have a number of possible causes, so it’s often difficult to say if HIV treatment is the cause.</p>
<li><b>Tiredness</b></li>
<p>Tiredness (<b>fatigue</b>) is a possible side-effect of most anti-HIV drugs.</p>
<p>As with most other side-effects, the risk of fatigue is greatest in the first few weeks after you start treatment with a drug, and then it is likely to lessen or go away completely.</p>
<p>It makes good sense to tell your doctor if fatigue is a problem for you.</p>
<p> If the cause is your HIV treatment there is a good chance that something can be done about it.</p>
<p> Fatigue can have other causes as well, so tell your doctor about prolonged tiredness so he or she can investigate possible causes.</p>
<p>Getting enough sleep, eating a good balanced diet, and regular exercise can also help with fatigue.</p>
</ol>
