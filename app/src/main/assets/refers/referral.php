  <h4><b>Referral vs Referral System vs Linkage</b></h4>

  <li><b>Referral</b></li> 
<p>The term <b>“referral”</b> is used to describe a process of assisting youth in obtaining preventive health services through a variety of activities, including, but not limited to, connecting young people to youth-friendly providers and support services.</p> 
 <li><b>Referral System</b></li> 
 <p>A set of resources and processes to increase youth awareness of health services, increase referral and increase the number of adolescents receiving YFS.</p>

  <li><b>Linkage</b></li>
<p>A system that enables combination and integration of health Care services.</p> 
<p>Linkage ensures comprehensive and complete access of health care services.</p> 

<h4><b>Goal</b></h4>

<p>To provide all young people with services they trust and which they feel are designed for them.</p> 
<p><b>Key components of youth-friendly health care services include:</b>
<ul> 
<li>Providing accessible location and hours</li>
<li>Creating a warm and respectful environment</li>
<li>Maintaining confidentiality</li>
<li>Ensuring opportunities for private conversations</li>
<li>Providing age-appropriate explanations for adolescents to give informed consent for services</li>
<li>Offering comprehensive services. </li>
<li>Encouraging youth to involve parents/guardians while respecting their privacy.</li>
</ul>
</p>

<h4><b>Types of Services to Consider Including in your Referral System:</b></h4> 

<p>Reproductive Health (e.g., <b>family planning, STI testing and treatment, HTS, RH counseling, PMTCT, ART etc.</b>)
<ul> 
<li> Mental Health (e.g., depression, suicide, etc.) </li>
<li> Substance Abuse </li>
<li> Primary Care (e.g., well check, vaccines, etc.) </li>
<li> Non-health care services (e.g., housing, education, job training, etc.)</li> 
</ul></p>

<h4><b>Core Components of a Referral System</b></h4> 
<ul>
<li><b>Policy:</b> Highlight and share confidentiality and consent policies for minors, develop policy assessments, conduct gap analysis and educate key decision-makers and stakeholders.</li> 
<li><b>Referral staff:</b> Identify and select designated referral staff, conduct staff training, ensure organizational engagement and awareness and plan for self-referrals.</li> 
<li><b>Procedures:</b> Determine who should make a referral, when a referral can be made, what steps are involved in making a referral and how to make a referral. </li>
<li><b>Referral guide:</b> Develop, design, produce, and publish your own guide, and determine best practices for updating and disseminating the guide. </li>
<li><b>Communications and marketing:</b> Develop a plan, engage adolescents and ensure school faculty/community and staff awareness. </li>
<li><b>Monitoring and evaluation:</b> Determine options for monitoring and evaluating, as well as questions for guiding the development of the monitoring and the evaluation system. </li>
<li><b>Management and oversight:</b> Identify core tasks for the management and oversight team. </li>
</ul>
