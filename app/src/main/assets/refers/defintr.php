<h4><b>ANTIRETROVIRAL THERAPY (ART)</b></h4> 
<p>These are Medications used to treat HIV reduce the ability of the virus to replicate (or reproduce) within the body. When HIV is unable to replicate, damage to the immune system and other vital organs is reduced, usually leading to improved health.</p> 
<p>Patients receiving antiretroviral therapy are less susceptible to opportunistic infections, malignancies, and other illnesses. Many patients live longer as a consequence of antiretroviral therapy.</p>
