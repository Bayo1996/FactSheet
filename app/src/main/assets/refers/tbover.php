<p><b>Tuberculosis (TB)</b> is caused by bacteria (<b>Mycobacterium tuberculosis</b>) that most often affect the lungs.</p>
<p>Tuberculosis is curable and preventable.</p>
<p>TB is spread from person to person through the air. When people with lung TB cough, sneeze or spit, they propel the TB germs into the air.</p>
<p>A person needs to inhale only a few of these germs to become infected.</p>
<p>When a person develops active TB disease, the symptoms (such as <b>cough, fever, night sweats, or weight loss</b>) may be mild for many months.</p>
<p> This can lead to delays in seeking care, and results in transmission of the bacteria to others.</p>
<p> People with active TB can infect 10â€“15 other people through close contact over the course of a year.</p>
<p> Without proper treatment, 45% of HIV-negative people with TB on average and nearly all <b>HIV-positive</b> people with TB will die.</p>
<h4><b>Who is most at risk?</b></h4>
<p>Tuberculosis mostly affects adults in their most productive years.</p>
<p> However, all age groups are at risk.</p>
<p> Over 95% of cases and deaths are in developing countries.</p>
<p>People who are infected with HIV are 20 to 30 times more likely to develop active TB.</p> 
<p>Tobacco use greatly increases the risk of TB disease and death.</p>

