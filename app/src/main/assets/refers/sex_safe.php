<h3><b>SAFER SEX</b></h3>
<p>Practicing safer sex allows partners to reduce their sexual health risks. The basic rules for safer sex are to prevent contact with genital sores as well as the exchange of body fluids, such as semen, blood, and vaginal secretions. 
<b>Practicing safer sex</b> all the time is very important because infections are usually spread between partners who have no symptoms of infection. <br>The most effective means of preventing pregnancy and STIs is abstaining from oral, vaginal, or anal sex.</p>
<p><b>Sexually active individuals</b> can reduce their risk of STIs by reducing the number of sexual partners they have, by engaging in <b>less risky behaviors, and by using latex condoms. </b><br>
<b>Reducing Partners The fewer partners a person has in their lifetime and the longer he/she delays beginning to have sex, the lower his/her risk of transmitting or contracting HIV or other STIs.</b>` 
<b><br><br>It is safest to practice monogamy</b> (having only one partner) with an uninfected partner. <b>Partners do not need to worry about STIs if neither partner has ever had sex with anyone else, has ever shared needles with anyone else, or has ever had an STI. </b>
<br><br>The only way to know this for sure, however, is for<b> both partners to be TESTED for STIs and HIV</b> by a health-care provider.<br>
It is important to remember that individuals may not know they had an <b>infection, may not be totally honest about their sexual history, or may have been born with HIV. </b>
In addition, <b>having concurrent partners </b>(more than one partner at the same time) greatly increases a person’s risk of <b>contracting STIs, including HIV. </b></p> 
<p><b>Sexually active individuals should consider being tested for STIs and HIV at regular intervals or before they begin a new sexual relationship. </b>
<b>Engaging in Safer Behaviors</b> Is another way to reduce the risk of contracting an STI is by engaging in lower risk sexual behaviors like <b>masturbation, manual stimulation, body rubbing, and kissing.</b> 
<br><br>Again, <b>to prevent STIs</b> it is important to <b>AVOID CONTACT</b> with body fluids, including <b>blood and semen, as well as any sores or growths</b> caused by STIs. 
It is also important to <b>remember</b> that people can come into contact with blood through other non-sexual behaviors such as sharing needles (for drug use or tattoos). <br><br>
<b>Avoiding these behaviors</b> is also essential to preventing HIV in particular. Using Condoms Sexually active individuals can help prevent STIs by using condoms. <br>
<ul>
<li><b>Male latex condoms, when used consistently and correctly, are highly effective in reducing the transmission of HIV and other STIs.</b></li></p>
 <p><li><b>Female condoms</b> can also reduce the risk of STI transmission when used correctly</li></p>
 </ul>
<p><b>ABSTINENCE,</b> also called <b>“celibacy,”</b> can be defined differently by different people.  Some people define it as not having vaginal intercourse while others use the term to mean not engaging in any kind of sexual behavior with another person.</p>
<ul> 
  <li><b>Correct Use:</b> For the purposes of preventing pregnancy partners must avoid vaginal intercourse.</li> 
   <li><b>Effectiveness:</b> Abstinence can be 100 percent effective at preventing pregnancy if partners never engage in vaginal intercourse.</li>
   </ul> 
<p><b>STD prevention:</b> Abstinence from vaginal intercourse can also reduce individuals’ risk of contracting an STI, though it is important to remember that sexual behaviors other than vaginal intercourse can carry some risk of STIs.</p>
</ul>
