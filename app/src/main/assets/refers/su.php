<h4><b>Should pregnant women get tested for HIV? How is testing done?</b></h4>
<p>Young women who are planning on becoming pregnant or who are pregnant should be tested for HIV as soon as possible. The woman’s partner should also be tested.
<p><strong>Health care provider may offer testing and counseling or may refer her to a local testing sight.</strong></p>
<h4><b>How can HIV/AIDS affect my pregnancy?</b></h4>
<p>In most cases, HIV will not cross through the placenta from mother to baby. If the mother is healthy in other aspects, the placenta helps provide protection for the developing infant. Factors that could reduce the protective ability of the placenta include in-uterine infections, a recent HIV infection, advanced HIV infection or malnutrition.</p>
<p>Unless a complication should arise, there is no need to increase the number of prenatal visits. Special counseling about a healthy diet with attention given to preventing iron or vitamin deficiencies and weight loss as well as special interventions for sexually transmitted diseases or other infections (such as malaria, urinary tract infections, tuberculosis or respiratory infections) should be part of the prenatal care of HIV infected GIRL.</p>
<p>Health care providers should watch for symptoms of AIDS and pregnancy complications of HIV infection. 
<h4><b>What is the chance that my baby will become HIV positive?</b></h4>
<p>A baby can become infected with HIV in the womb, during delivery or while breastfeeding. If the mother does not receive treatment. Its the responsibility of the pregnant woman to protect her child</p>
<h4><b>How will my prenatal care be handled differently if I am HIV positive?</b></h4>
<p>A multi-care approach is the most effective way for pregnant women with HIV infection to have a healthy pregnancy and delivery. This approach will address the medical, psychological, social and practical challenges of pregnancy with HIV. While the woman’s pregnancy is being managed by a health care provider and HIV specialist, she may also receive assistance from a social services agency to help her with housing, food, child care and parenting concerns.</p>
<p>She would also be receiving counseling support for herself and her partner. Additional care could be provided in the areas of substance abuse and lifestyle counseling. This team effort will provide the best prenatal care plan for women infected with HIV. Many of these services could continue during her postpartum period.</p>
<p>Zidovudine (also known as ZDV, AZT and Retrovir®) was the first drug licensed to treat HIV. Now it is used in combination with other anti-HIV drugs and is often used to prevent perinatal transmission of HIV. ZDV should be given to HIV-infected women beginning in the second trimester and continuing throughout pregnancy, labor and delivery. Side effects include nausea, vomiting and low red or white blood cell counts.</p>
<h4><b>How does HIV affect my labor and birth?</b></h4>
<p>If no preventative steps are taken, the risk of HIV transmission during childbirth is estimated to be 10-20%. The chance of transmission is even greater if the baby is exposed to HIV-infected blood or fluids. Health care providers should avoid performing amniotomies (intentionally rupturing the amniotic sac to induce labor), episiotomies and other procedures that expose the baby to the mother’s blood. The risk of transmission increases by 2% for every hour after membranes have been ruptured.</p>
<h4><b>Will my baby need treatment after delivery?</b></h4>
<p>A 1994 study by the National Institutes of Health found that giving ZDV to an HIV-positive pregnant woman during her pregnancy and to her baby (within 8-12 hours of birth) decreased the risk of passing the infection on to the baby by 66%. The baby should be treated with ZDV for the first six weeks of life. Eight percent of babies of women treated with ZDV became infected, compared with 25 percent of babies of untreated women.</p>
