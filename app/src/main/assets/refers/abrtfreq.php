  <h4><b>FREQUENTLY ASKED QUESTIONS ABOUT ABORTION</b></h4>
<p><b>What is a safe abortion? </b><br> 
  Safe abortion is termination of a pregnancy by a health care provider qualified in providing the services while observing proper health standards.</p>
  <p><b>What is an unsafe abortion? </b><br> 
The World Health Organization defines unsafe abortion as a procedure for terminating an unintended pregnancy carried out either by persons lacking the necessary skills or in an environment that does not conform to minimal medical standards, or both. 
Unsafe abortions may lead to complications such as perforations of the uterus, retained placentas, severe bleeding, cervical wounds, rampant infections, poisoning, shock and gangrene.</p> 
<p><b>What is post-abortion care? </b> <br>
In settings where abortion is legally restricted or access to services is limited, women with unwanted pregnancies often resort to unsafe abortions and subsequently require urgent medical attention to treat complications. Women may also suffer incomplete spontaneous abortions (miscarriages) or complications from a safe abortion that require medical attention. </p>

<p><b>Post abortion care,</b> a term originated by Ipas in the early 1990s, is an approach for reducing deaths and injuries from incomplete and unsafe abortions and their related complications.  Post abortion care is an integral component of comprehensive abortion care and includes five essential elements:</p> 
<ul>
<li>Treatment of incomplete and unsafe abortion and complications</li> 
<li>Counselling to identify and respond to women’s emotional and physical health needs </li>
<li>Contraceptive and family-planning services to help women prevent future unwanted pregnancies and abortions</li> 
<li>Reproductive and other health services that are preferably provided on-site or via referrals to other accessible facilities </li>
<li>Community and service-provider partnerships to prevent unwanted pregnancies and unsafe abortions, to mobilize resources to ensure timely care for abortion complications, and to make sure health services meet community expectations and needs. </li>
</ul>

<p><b>What is the difference between a spontaneous and induced abortion? </b><br>  
<b>Spontaneous abortion </b>is the loss of pregnancy before 20 weeks of gestation without outside intervention.  
Induced abortion, or abortion by choice, is a procedure that is done to end a pregnancy. Most induced abortions are done in the first 12 weeks of pregnancy. </p>
<p><b>Who may provide safe abortions? </b> <br> 
According to Kenyan law, safe abortions may be provided by qualified health care providers while adhering to required medical standards.  Kenyan Law and Abortion According to Kenya law,<b> an induced abortion may be performed on recommendation of a qualified health service provider. According to article 26 (4) of the Constitution of Kenya “abortion is not permitted unless, in the opinion of a trained health professional, there is need for emergency treatment, or the life or the health of the mother is in danger, or if permitted by any other written law.”</b></p>
<p><b>Can a woman get pregnant after having a safe abortion?</b><br>  
Yes, it is possible if they have sexual intercourse without using any contraceptive method.</p> 
<p><b>How long will a woman feel pain after having a safe abortion?</b><br> 
Women experience very little pain when abortion is done in a safe way by a qualified health care provider at a health facility. Minimal discomfort, however, may last for about three days. 
How soon can a woman start having sex after having an abortion?  
As soon as she feels comfortable having sex, but it is recommended that she doesn’t have sex for one week and avoid putting anything like tampons in the vagina to reduce chances of getting infections. </p>

<p><b>What are the chances that a woman could die if she has safe abortion? </b> <br>
Chances of a woman dying from safe abortion are almost nil if done by a trained health care provider. However complications may arise as a result of other previous medical issues a client may have had but not from the abortion itself. </p>
<p><b>Can there be complications after having a safe abortion? </b> <br>
Chances of complications are very low after having a safe abortion. In case of any complications like severe bleeding, one is advised to seek medical attention. </p>
<p><b>Can safe abortion procedure interfere with a woman’s walking style?</b><br> 
No, it does not. But rarely, the discomfort from the procedure may temporarily make normal walking a challenge.</p> 
<p><b>Can safe abortion lead to a woman having subsequent pregnancy growing out of the uterus and suffering miscarriage? </b> <br>
No, safe abortion does not harm a woman in any way neither does it cause ectopic pregnancies. After having safe abortion, a woman is able to conceive any other time she chooses to, carry the pregnancy in a normal way to term and deliver a healthy child if there is no other medical condition that affects future fertility . </p>
<p><b>Does abortion mean removal of the uterus?</b> <br> 
No, safe abortion is not the removal of the uterus. Safe abortion is the removal of the products of a pregnancy from the uterus and not the removal of the uterus. </p>
<p><b>Can a health care provider be arrested for performing an abortion? </b><br> 
One cannot be arrested for performing safe abortion if the person is a trained health care provider working within a health facility that meets the set standards of health. According to the Kenya constitution, safe abortion can be performed by a health care provider if the mental, social and physical health of a woman is in danger. A health care provider is a trained doctor, clinical officer, nurse or a midwife. </p>
<p><b>Can one be arrested for seeking a safe abortion?</b><br>  
According to the Kenya constitution abortion is not permitted on demand however safe abortion can be performed by a health care provider if the mental, social and physical health of a woman is in danger. 
Women seeking abortions should consult a trained health care provider to determine if induced abortion is an option for her.  Seeking abortion outside of the formal health care system can put women at risk of arrest. </p>  


