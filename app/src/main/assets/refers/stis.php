<p><b>STIs</b> can be caused by <b>bacteria, viruses or parasites</b>.</p>
 <p>More common STIs include:</p>
 <ul>
 <li><b>Chlamydia</b>, including Lymphogranuloma Venereum (LGV)</li>
 <li><b>genital and anal warts</b> (caused by the human papillomavirus, or HPV).</li>
 <li><b>gonorrhoea</b></li>
 <li><b>hepatitis A, B and C</b> </li>
 <li><b>herpes</b> (caused by the herpes simplex virus, or HSV). This can cause both oral (on the mouth) and genital infection</li>
<li><b>syphilis</b></li>
 <li>parasites such as <b>pubic lice</b> (sometimes called crabs) and scabies</li>
<li><b>shigellosis</b>, an illness caused by the bacteria Shigella, and other illnesses that cause diarrhoea</li>
<li><b>trichomoniasis</b>, an infection caused by a very small parasite.</li>
</ul>

<h4><b>Common symptoms</b></h4>
<p>There are some common symptoms of STIs.</p>
<p><b> These include:</b></p>
<ol>
<li>a discharge from your vagina, penis or anus. This may be discoloured – for example, milky, yellowish or like mucus – or may have blood in it</li>

<ll>pain or a burning feeling when you urinate (pee) or needing to urinate more often than usual
pain while you are having sex</ll>
<li>pain or swelling around your anus or testicles</li>
<li>generally feeling unwell</li>
<li>for <b>herpes</b>, numbness, itching and tingling, followed by bumps that become small, fluid-filled blisters</li>
<li>for <b>syphilis</b>, small sores, spots or ulcers on the penis or around the mouth, vagina or anus</li>
<li>for <b>hepatitis A, B and C</b>, symptoms can include jaundice (a yellowing of skin and eyes), nausea and vomiting, and tiredness</li>
<li>for <b>genital warts</b>, lumps and bumps around the genital area and anus
diarrhoea with blood and pain.</li>
</ol>
<h4><b>Treatment</b></h4>
<p><b>Bacterial infections and trichomoniasis</b> can be cured with antibiotics.</p>
<p> These may be given as tablets or by injections, depending on the STI you have.</p>
<p>Antiviral drugs can be used to treat some viral infections.</p>
<p> Herpes cannot be cured and the virus stays in nerve cells for life, although most of the time it may not cause symptoms.</p>
<p> You may have <b>flare-ups</b> from time to time, especially if you have a weakened immune system.</p>
<p> Your immune system may cure <b>HPV</b>, the genital warts virus, but this may take a long time and for some people never happens.</p>
<p><b>Lotions</b> can clear infestations of parasites such as scabies or pubic lice, along with washing clothing, towels and bedding at high temperatures.</p>
<p>Any sexual partners you have had since the time you may have been infected should also go for a sexual health check, as they may also need to be treated.</p>

<h4><b>Transmission and prevention of STIs</b></h4>
<p>STIs can be transmitted through <b>anal, oral and vaginal sex, and by sharing sex toys</b>.</p>
<p> Some can also be passed on through rimming (mouth-to-anus contact), kissing or other close physical contact.</p>
<p> Parasites can be passed on by sharing towels or bedding.</p>
<p> Some STIs (<b>including hepatitis A</b>) and other infections (for example, <b>gut infections such as giardia</b>) can be caused by contact with infected faeces (excrement, shit), such as during rimming or fisting.</p>
<p>Hepatitis B is passed on by <b>contact with the blood, semen, saliva, or vaginal fluids</b> of a person with hepatitis B.</p>
<p> It is easily passed on during sex without a condom and from a mother to her baby during delivery.</p>

<p>Hepatitis C is normally passed on through <b>blood-to-blood contact.</b></p>
<p><b>Using condoms</b> during anal or vaginal sex, using a condom or dental dam for oral sex, and not sharing sex toys can protect you against most STIs.</p>
<p> If you are fisting, to protect yourself against hepatitis C, wear <b>latex gloves</b> and do not share pots of lubricant..</p>
<p> With some STIs, using a condom or dental dam will reduce the risk of infection, but not protect you completely.</p>
<p>There are <b>vaccines available</b> for hepatitis A and B.</p>
<p> Unless they have a natural immunity to these viruses, people with HIV are recommended to be vaccinated against them.</p>
<p> You may have these vaccinations at your GP surgery rather than your HIV clinic.</p>
<p> If your immune system becomes weaker, you may need to have your immunity level checked.</p>
<p>If you have been diagnosed with an STI, or are having a flare-up of herpes symptoms, you may be advised not to have sex (even with a condom) until any treatment is finished, and sometimes for a while after that.</p>

