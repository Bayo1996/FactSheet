<p>Many people with HIV need to take medicines to treat other health conditions</p>
<p><b> Taking two or more different drugs together</b> may result in an alteration in the effectiveness or in the side-effects of one or more of the drugs.</p>
<p> Some drugs should <b>NOT</b> be taken in combination with certain antiretrovirals.</p>
<p>It is important that anyone prescribing or dispensing medication knows about all other medicines and drugs that you are taking – this includes those prescribed by another doctor, over-the-counter medicines (including inhalers and nasal sprays), herbal and alternative treatments, and recreational drugs.</p>
<p><b>Some drug combinations are contraindicated</b> – which means you definitely should not take them together.</p>
<p> Reasons for this include serious side-effects, or interactions which make one or both drugs ineffective or toxic.</p>
<p>Other interactions are less dangerous, <b>but</b> still need to be taken seriously.</p><p> Levels of one or both drugs in your blood may be affected and you may need to change the doses you take.</p>

<p>You also need to tell your HIV doctor about any drugs you buy over the counter (at a chemist, for example) or from the internet.</p>
<p> Some anti-HIV drugs can interact with <b>antihistamines, asthma inhalers or nasal sprays</b> (which contain <b>steroids</b>), treatments for <b>indigestion and statins</b> (drugs that are used to control cholesterol, or lipid levels).</p>
<P> These treatments can either be prescribed or bought over the counter at high-street chemists (community pharmacies).</P>
<P>If you are thinking of using any other drugs, you should tell your HIV doctor or pharmacist so they can check for possible interactions and recommend the most suitable treatment.</P>
<p> Or, when you are buying them, you may wish to tell the pharmacist about the anti-HIV drugs you are taking.</p>
<p> Community pharmacies often have a private area for consultations, or you could write the name of the drugs down and hand them to him or her.</p>
<p> If you do need to mention the name of your anti-HIV drugs, it’s very unlikely that anyone around you will recognise what they are used to treat.</p>
<p>In many cases, the interactions are theoretical, or seen in test-tube studies, and more information is needed about the likelihood of a real-life effect.</p><p> For example, <b>test-tube studies have indicated that African potato</b> and
<p><b>Sutherlandia</b> may reduce levels of PIs, NNRTIs and maraviroc (Celsentri) in the body.</p>
<p>Interactions can also happen with medicines that are not taken by mouth.</p>
<p>You can safely take some <b>painkillers</b>, such as paracetamol, when on anti-HIV drugs, unless there are other medical reasons why you shouldn’t take this sort of drug.</p>
<p> Check with your HIV doctor or pharmacist about the best type of painkiller for you.</p><br><br>