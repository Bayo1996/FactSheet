<h3><b>What is premenstrual syndrome?</b></h3> 
<p>These are physical or emotional symptoms that women may experience before their menstruation. Symptoms include:</p> 
<ol>
<li>Aches and feeling heavy</li> 
<li>Acne (Acne vulgaris is the most common in teenagers.) </li>
<li>Fever </li>
<li>Bloating </li>
<li>Cramps/ abdominal pain</li> 
<li>Mood swings and anxiety </li>
<li>Increased clear vaginal discharge </li>
<li>Temperature rise </li>
<li>Sore breasts</li> 
</ol>
