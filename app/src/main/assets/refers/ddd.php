<h4><b>Disclosure to Medical and Other Healthcare Providers</b></h4> 
   <p>Your primary care physician needs to know about your HIV status. But in the course of your life, you will likely have occasions to visit different doctors and other health care professionals, sometimes for health matters that are not related to HIV. With other health care providers you have more of a choice whether or not to disclose.</p>
   <p> For instance, there's no particular reason your podiatrist needs to know you are HIV positive. But in the case of your optometrist or your dentist, while you're not legally bound to disclose, by having that information he or she may be able to identify certain health problems.</p>
   <p>All medical providers are supposed to be take "universal precautions," which means special procedures to protect themselves against any transmissible infection, not just HIV.</p>
   <p>Health care providers cannot deny their services to someone simply because the person is HIV-positive. If a doctor or other health care provider is uncomfortable treating someone with HIV and makes that known in whatever way to you, be aware that you have legal recourse in such situations.</p>
  <p>As far as disclosure of your HIV-related information is concerned, bear in mind that it's generally permitted only after you have signed an approved special HIV release form.</p>
<h5><b>General tips to consider with regard to medical and health care providers:</b></h5>
  <ol>
  <li>All health care providers are bound by confidentiality laws.</li>
  <li>By telling a doctor, a nurse or other health care providers, you do give up a degree of privacy, but that does not release them from adhering to laws regarding confidentiality.</li>
  <li>Your status should be treated as privileged information. If, for instance, a doctor's employee discusses details with you that another patient might overhear, politely request that such conversations be discussed in private.</li>
  <li>A hospital or other health care provider may share HIV information with a patient's insurance company if the information is necessary to pay for medical care.</li>
  <li>If you're in doubt about whether you have to reveal your status for either medical or insurance purposes, or indeed legally for any other reason, call your local Department of Health or AIDS service organization. In some instances you may learn that it's necessary to disclose in order to have access to medical resources and services.</li>
  </ol>
