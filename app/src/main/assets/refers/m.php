    <li><p><b>What is the HIV prevention pill? </b>
The HIV prevention pill is a pill that HIV-negative people take every day to significantly reduce their chances of getting HIV.  It is also called PrEP (which stands for Pre-Exposure Prophylaxis where “prophylaxis” means “prevention of disease”).  The HIV prevention pill combines two different antiretroviral (ARV) drugs in one pill. Truvada is the brand name of the HIV Prevention Pill. The two ARV drugs in Truvada are the drugs commonly used to treat HIV infection in HIV-positive people. However, when used for HIV treatment, they are always combined with even more ARVs. This is because scientists think that more ARV drugs are required to treat HIV than to prevent it.</p></li> 
 
<li><p><b>How is the HIV Prevention Pill (PrEP) different from Post-Exposure Prophylaxis (PEP)?</b> The HIV Prevention Pill, or PrEP, is not the same as PEP (which stands for Post-Exposure Prophylaxis). While both are taken by HIV-negative people to prevent HIV, PrEP is taken to reduce the risk of getting HIV from the potential exposure that may occur sometime in the future. It should be taken once every day so when a person comes in contact with HIV, his or her body is ready to fight off the virus. PrEP should be taken continuously for as long as a person remains at high risk of HIV (for example, when one’s partner is HIVpositive, or when a person has multiple sex partners). </p>
 
<p>PEP, on the other hand, is taken only after a person might have been exposed to HIV (for example a condom broke or was not used with a partner who is possibly HIVpositive, or a person was raped). PEP has to be started within 3 days after possible</p> 
<p>HIV exposure and involves taking a full treatment dose of ARV drugs two or three times a day for 28 days. Because of the higher dose of ARVs, side effects are more common with PEP than with PrEP, and these side effects are more pronounced. </p>
 
<p>PEP is a good choice for those who may experience rare, accidental exposure to HIV, while PrEP is a good choice for people who have an ongoing high risk of HIV exposure.</p> 

