<h4><b>Hormonal contraception and anti-HIV drugs</b></h4>
<p>Some types of contraception contain one or more hormones.</p>
<p> The extra hormones stop ovulation, so your body does not release an egg for fertilisation.</p> <p>They also thicken the mucus around the cervix, making it difficult for sperm to get into the womb.</p>
<p><b>They include:</b></p>
<ul>
<li>the combined pill</li>
<li>the progestogen-only pill</li>
<li>the patch</li>
<li>the vaginal ring</li>
<li>Depo-Provera injections</li>
<li>the implant</li>
<li>the intrauterine system (<b>IUS, Mirena or
Jaydess</b> ).</li>
</ul>

<p><b>Long-acting contraceptives</b></p>
<p>Some contraceptive methods work for long periods at a time.</p> 
<p>These include the <b>implant, the intrauterine system (IUS, Mirena or Jaydess ), and Depo-Provera injections (all hormonal methods) and the intrauterine device</b>.</p>
<p> With the exception of the implant, these can be used by women taking any anti-HIV drug.</p>

<p>Many people with HIV choose to <b>use condoms</b> .</p>
<p> Condoms, properly and consistently used, are an effective way to avoid passing on HIV to a partner.</p>
<p> Condoms also offer protection from other sexually transmitted infections – the only form of contraception that does.</p>

<p>Used correctly every time you have sex, condoms are an effective way of preventing pregnancy. </p>
<p>However, condoms may break or come off. Many people choose a strategy of <b>‘double protection’</b> and use condoms in addition to another, more reliable, form of contraception.</p>
<p>There are both male (i.e. used by men) and female (used by women) condoms.</p>
<p> It is very important you follow key steps to make using a condom as effective as possible.</p> <br><br>