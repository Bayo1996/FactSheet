<p>At the age of four or five your child will start going to school.</p><p> This is initially an unsettling experience for a lot of children, but most soon settle in, make friends, and enjoy the stimulation that school provides.</p>
<p>Schools are not allowed to discriminate against children because they have HIV.</p>
<p>Children with HIV should be treated like the other children in the school, and allowed to take part in all school activities, depending only on how well they are at any time.</p>
<p> The school or local authority should have a policy on managing medicines in schools and supporting children who have medical needs, including issues of confidentiality.</p>
<p><b>Hospital appointments and illness</b> may mean that your child has absences from school.</p> <p>You don’t have to mention HIV when you provide an explanation to the school about these absences.</p>
<p> If children are doing well on treatment, they will usually only need to have three or four visits to their clinic during a year.</p>
<p> You can also try to schedule some of these during school holidays, so children may not need to miss much time from school.</p>
<p> However, regular or lengthy absences can have an impact on your child’s learning.</p><p> If this is an issue, talk to staff at the school about additional educational support for your child.</p> <p>Staff at your clinic or an HIV support organisation can also provide advice on this.</p>
<p>Research has shown that some children with HIV have experienced some damage to <b>neurocognitive (brain) function</b>.</p>
<p> This can lead to slower educational development than seen in other children.</p>
<p> There are likely to be a number of reasons for this, including both the child’s physical health and their family history and circumstances.</p>
<p> Support from a child psychologist or specialist educational support workers may be helpful and can be accessed through your child’s HIV clinic or the school.</p>
<p>If you are discussing your child’s needs with the school, you <b>don’t have to mention HIV.</b></p>

