<p><b>Long-term</b> alcohol use can increase the risk of serious health conditions.</p>
<p>Alcohol can damage the liver which plays an important role in how the body processes anti-HIV drugs.</p>
<p>Drinking alcohol may be more harmful for people with HIV than for HIV-negative people.</p>
<p>Alcohol is a drug and comes in many forms, including <b>beer, cider, wine, ‘alcopops’, and spirits</b> such as whisky, gin and vodka.</p>
<p>Being in drinking can lead to <b>poor co-ordination, vomiting, exaggerated emotional reactions (including sadness, tearfulness, anger and aggression) and loss of memory</b>.</p><p> It can also lead to <b>heart problems, alcohol poisoning and unconsciousness</b>.</p>
<p>Drinking alcohol during pregnancy <b>can harm</b> the unborn baby, so women who are pregnant or planning to become so, are advised to avoid alcohol.</p>

<h4><b>Short-term effects of alcohol</b></h4>
<p>Alcohol relaxes the brain and body which some people find pleasurable.</p><p> Many people find that drinking helps relieve stress, encourages relaxation and acts as an appetite stimulant.</p>blems.
<p>A hangover – headache, dry mouth, feeling sick and tired – is a very common consequence of heavy drinking the night before.</p>
<p>Even small amounts of alcohol can have an effect on your co-ordination, reactions and judgments, including influencing decisions you make about sex.</p>
<p><b> You should never drink even small amounts of alcohol and drive, or operate machinery</b></p>.
<p>Extremely heavy drinking can lead to <b>coma and even death.</b></p>

<h4><b>Longer-term effects of alcohol</b></h4>
<p>Long-term heavy alcohol consumption can increase the risk of <b>serious health conditions</b>.</p> <p>These include a wide range of <b>cancers, heart disease, high blood pressure , stroke, liver disease, pancreatitis, depression, dementia,
sexual problems and infertility.</b></p>

<p>People who drink heavily often don’t eat well and this can cause further health problems.</p>
<p>Alcohol is a depressive drug and can cause or make mental, psychological or emotional problems worse.</p>
<p>Drinking too much can make it harder for you to take your drugs in the right way and at the right time .</p>
<p>Alcohol can cause vomiting.</p>
<p> If you vomit <b>within an hour of taking a dose</b> of your anti-HIV drugs then you should retake the dose.</p>
<p>Alcohol can damage the liver and heavy drinking can cause hepatitis.</p>

