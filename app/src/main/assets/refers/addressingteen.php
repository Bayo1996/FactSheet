	<h5><b>How can we address teenage pregnancy??</b></h5>

<p>We can address teenage pregnancy <b>by teaching</b> young people skills that will help them make <b>appropriate decisions, be assertive,</b> and <b>resist early sexual activity. </b></p>

<p>Sometimes it’s hard for a teenager to recognize the signs of pregnancy and they may want to hide a pregnancy when they realize that care is available or may not be able to afford the fees charged.</p>

<h5><b>How do I know if I’m pregnant?</b></h5>

<p>Pregnancy can only happen if semen (cum) gets in your vagina or on your vulva.</p>
<p> So if you haven’t had penis-in-vagina sex or had any semen (cum) near your vulva since your last period, you’re not pregnant.</p>
<p> Also keep in mind that it takes up to a few weeks for pregnancy to happen after sex.</p>
<p> So if the sex happened a few days ago, you can’t be pregnant yet, and actually there’s still something you can do to prevent it i.e. using contraceptives.</p>

<p>If you’ve had penis-in-vagina sex and miss your period, it’s possible that you’re pregnant.</p>
<ul>
<li><p>The first thing to do if you think you’re pregnant is <b>take a pregnancy test</b> — it’s the only way to know for sure.</p>
<p> A pregnancy test can tell you if you’re pregnant as soon as your period is late.</p>
</li> 
<li><p>Lots of things besides pregnancy can mess with the timing of your period, like stress, diet, changes in your weight, and being sick.</p>
<p> It’s also really common to have irregular periods when you’re a teenager, so try not to panic.</p>
</li>
<li><p>Early pregnancy symptoms include bloating, sore breasts, upset stomach, and feeling tired.</p>
<p>But these symptoms can also be caused by other things, like PMS or being sick. So symptoms alone can’t tell you what’s going on. Only a pregnancy test can.</p></li>
<li><p><b>If your pregnancy test is positive</b>, make an appointment with your doctor or local health facility right away so you can talk about your pregnancy options and make sure you stay healthy — no matter what you decide to do.</p></li></ul>
