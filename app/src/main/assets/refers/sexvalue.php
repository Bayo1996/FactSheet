<h4><b>Value System</b></h4>   
<p>Every society, community, family and therefore every individual, has values, which give direction to one’s life. <b>Values describe precise moral beliefs about how things really are.</b></p> 
<p><b>These include beliefs :</b></p>
<ul>
<li>About your place in the world,</li> 
<li>About how you should relate to others and about how others should relate to you;</li> 
<li>Beliefs about your responsibility to others and about other people’s responsibility to you;</li> 
<li>Beliefs about your own worth and about the worth of others</li> 
<li>Beliefs about the real worth of material things.</li> 
</ul>
<p>When you hold fast to whatever your beliefs are, the result is referred to as your <b>value system, which is what shapes your attitudes and your behavior.</b> 
<p>You get your values or your moral beliefs through the process of growing up and being exposed to the ways of significant persons, institutions and events in your life. For example your parents, your family as a whole, your church or religious organization, your school and teachers, your community, and also the particular things that happen to you, all make impressions on you and shape you. Each person develops a built-in value system.</p> 
<p>This does not mean that values remain fixed for a person’s entire life. A value system can change. 
The change is always influenced by significant persons and events, which make an impact on the person’s present values, or cause the person to question his or her existing values.</p>
<h4><b>Sexual values</b></h4> 
<p>Sexual behavior is an area of human life that is open to conflicting values and opinions. Just think of the moral questions surrounding acts or activities such as abortion, masturbation, homosexuality, pre- marital sex or extra-marital sex. Different groups of people have different precise moral beliefs about sexual practices and behavior. Young people are many times confused about values because of the wide range of conflicting behaviours in their society. Moral standards can differ, and peer helpers will sometimes have difficulty in remaining neutral because of their individual value systems.</p>

