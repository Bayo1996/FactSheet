<h4><b>PROGRAM</b></h4>
<h4><b>GOAL</b></h4>
<p><b>International Youth Alliance for Family Planning (IYAFP)</b> is a non-governmental organization with its headquarters in Washington DC championing for adolescent-youth sexual reproductive health and rights.</p>
<p>By 2020, IYAFP commits to reaching 6000 youth by supporting 15-30 year olds in their youth-led, youth-driven advocacy from the ground up.</p>
<p> We commit to equip youth with the skills to lead grassroots projects, create country-specific action plans, present at various meetings and conferences, and to make “youth voices heard” at all levels.</p>
<p> Youth participation in panels and presentations will inject a crucial perspective in these important meetings.</p>
<p> We also commit to targeted and effective advocacy by tracking national health policies and by training our youth on SMART advocacy to ensure that projects and in-country events are effective at improving knowledge, attitudes, and behaviors of youth on the ground.</p>

<p>The International Youth Alliance for Family Planning (IYAFP) commits to accomplishing the following by 2020:</p>

<p>Supporting youth (15-30 year olds) representation from over 85 countries at high-level convening’s, international processes, meetings and conferences and using such opportunities to promote the goals of FP2020, the SDGs, IYAFP and in support of EWEC to advance the Global Strategy. By providing the opportunities/platform and finances for youth to influence program and policy development and implementation thereby ensuring effective inclusion and engagement of youth as partners and not just beneficiaries.</p>

<h4><b>Outcome</b></h4>
<p>Strengthened youth representation at various meetings and conferences reaching a cumulative 500 youth with positive SRHR messaging via panels, presentations, blogging, and networking.</p>

<h4><b>POLICY</b></h4>
<p>Tracking national health policies and demanding a particular youth focus towards the removal of barriers (age restrictions, parental or marital consent, method restrictions based on parity, and financial) and support for enabling factors (comprehensive sex education, integrated youth friendly information and contraceptive and safe abortion services, free or discounted services, etc.)</p>

<h4><b>Outcome:</b></h4> 
<p>National health policies tracked in at least 10 countries for targeted advocacy via in-country technical working groups with IYAFP representation to change policy that will reduce barriers to SRHR for at least 500 youth</p>

