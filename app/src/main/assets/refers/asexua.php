<h5><b>What’s asexuality?</b></h5>
<ul>
<li>People who identify as asexual don’t really feel sexual attraction towards anyone. They may think other people are physically attractive, or they may want to be in romantic relationships with people — but they’re not interested in having sex or doing sexual things with other people. Asexual people sometimes use the word “ace” for short.</li>

<li>	Asexuality has nothing to do with romantic attraction. Many asexual people feel romantically attracted to people — so they may identify as asexual, and also as gay, lesbian, bisexual, or straight. They just don’t feel any desire to act on these feelings in a sexual way.</li>
<li>	Asexual people have emotional needs just like everyone else. Some asexual people have romantic relationships, and others aren’t interested in that. They get close to people or experience intimacy through ways other than sex.</li>
<li>	There are also people who don’t feel romantic attraction or want to be in romantic relationships — they may identify as aromantic. Being aromantic and being asexual are two separate things.</li>
<li>	Some asexual people do get aroused (turned on), but they don’t feel the desire to be sexual with other people. And some asexual people masturbate. But others may not feel arousal at all.</li>
<li>	It’s totally normal to go through times when you don’t want to have sex, but that doesn’t necessarily mean you’re asexual. And asexuality is not the same thing as being celibate. Celibacy is a choice you make, and asexuality is a sexual identity — who you naturally are.</li>
<li>	Like other sexual orientations, asexuality isn’t always black and white. There’s a spectrum between being sexual (having sexual attraction) and being asexual. Different people fall into different places on that spectrum. Some people who have very little sexual attraction to other people identify as gray-a. Some people who are only sexually attracted to people they’re in relationships with identify as demisexual. Want to know how someone identifies? Ask them.</li>
<li>	There is nothing “wrong” with people who are asexual, and there’s no evidence to support that people are asexual because of any kind of mental health or trauma. It’s actually kind of common — some research says that 1 out of 100 adults is asexual.</li>
</ul>

<p><b>What if I don’t want to be labeled?</b>
	<ul>
<li>	It’s okay if you don’t want to be labeled. Only you can decide what sexual identity best describes you. But some people may feel that none of the common labels feel right to them.</li>
<li>	Your sexual orientation and identity can remain the same throughout your life. Or it can vary depending on who you’re attracted to, or romantically partnered or sexually active with. This is completely normal. Once you claim a label, there’s no reason why it can’t change as you change.</li>
<li>	Changing how you identify doesn’t mean that you’re “confused.” Many folks, old and young, experience changes in who they’re attracted to and how they identify. This is called <b>“fluidity.”</b> </li>
</ul>
