<h4><b>INTRODUCTIONS</b></h4>
<p>Basically there are 2 types of family planning/contraceptives;</p>
a) &nbsp;&nbsp;&nbsp;&nbsp;<b>Hormonal methods;</b><br>
These are methods containing synthetic hormones (oestrogen and progestin or a combination of both)</p>
<img src ="file:///android_asset/refer/images/pi.png" style ="width:100%">
<br>
• &nbsp;&nbsp;&nbsp;They mainly work by preventing ovulation and may cause hormonal side effects i.e. changes in monthly bleeding, weight or mood changes , nausea, headaches, dizziness, breast tenderness<br> 
• &nbsp;&nbsp;&nbsp;Examples; emergency pills, everyday pills, injections and implants.<br><br>
b)  &nbsp;&nbsp;&nbsp;&nbsp;<b>Non-hormonal methods</b><br>
•&nbsp;&nbsp;&nbsp; Do not contain hormones<br>
•&nbsp;&nbsp;&nbsp; Work by preventing fertilization<br>
•&nbsp;&nbsp;&nbsp; Examples; condoms and IUCDS<br>

