<h5><b>WHAT SHOULD I DO IF SOMEONE SEXUALLY ASSAULTED, ABUSED, OR RAPED ME?</b></h5>
<p>If you experienced sexual assault, abuse, or rape, you may be feeling shocked, scared, and uncertain of what to do next.</p>
<p><b>Here’s what to do right away:</b></p>
<ol>
<li><b>Get to someplace safe as soon as possible.</b> Your safety is important. Depending on where you are, you might want to get to an area with more people, or find someone to help you.</li>
<li><p>Once you’re in a safe place, <b>don’t change anything on your body.</b></p>
<p> You don’t have to decide right away if you’re going to talk with the police or press charges.</p>
<p> But just in case you do, it’s super important that the police have the evidence that may be on your body.</p> <p>So don’t take a shower or bath or wash off any parts of your body. Also if you can, don’t go to the bathroom, comb your hair, eat, smoke, drink, or take any drugs.</p>
<p> If you change your clothes, take the clothes you were wearing during the assault to the hospital or police department in a paper bag.</p></li>

<li> <b>Tell a parent, guardian, or another adult in your life who you trust.</b> They can help you figure out what to do next, which might include seeing a doctor or nurse for an exam or calling the police.</li>

<li><p><b> See a doctor or nurse.</b> Medical care is important after a sexual assault.</p><p> You can go to the emergency room or you may be able to go to any local government health facility where you will find CARE centers.</p></li>

  
  <p>The doctors and nurses who take care of people after sexual assault are usually specially trained.</p>
  <p> They know how to be gentle, caring, and sensitive. They will:</p>
<ul>
<li>Examine you to see if you were harmed in any way</li>
<li>Give you medical care that may include emergency contraception (if there is a risk of pregnancy), tests for STDs, and medicine to help prevent HIV (post-exposure prophylactics).</li>
<li>Collect evidence, so you have the option to press charges if you decide that’s right for you. Evidence might include semen, hair, or skin cells from the person who assaulted you.</li>
</ul>

<li><p><b>Get help from an expert.</b> </p>
<p> The Center for Assault Recovery of Eldoret offers a 24-hour inpatient and outpatient services, you can also call them through  hotline no <b style="color: blue">+254 532033471</b> .</p> 
<p>They can tell you about your options and connect you with local resources. A rape crisis center in your area can help you find a doctor or nurse, counselor, and other support.</p></li> 
<li><p><b>Decide if you want to talk to the police.</b></p>
 <p><b>Sexual assault</b> is a crime, and you have the right to report it to the police if you want to.</p>
 <p> You can call them yourself or ask a rape crisis counselor or a parent, guardian, or someone else you trust to do it for you.</p>
 <p> The police will ask you questions, and they’ll also talk to you about whether or not you want to press charges.</p>
<p>If you choose to speak with the police, you may want to have someone with you for support.</p>
<p> If a police officer asks to speak with you alone, you don’t have to unless you want to.</p>
<p> You can have someone you trust like a family member or friend there, or have a trained advocate with you.</p>
</li> 
<li><p> No matter what, <b>remember that what happened wasn’t your fault.</b></p>
<p> Sometimes people think it’s their fault if the attacker is a friend, family member, or person they were dating.</p>
<p> It’s still not your fault in any of those situations.</p>
<p> Even if you started doing something sexual with this person but didn’t want to continue and they forced you anyway, it’s still not your fault.</p>
<p> What you were wearing or drinking or how you were acting doesn’t make it your fault. You didn’t ask for this and you didn’t deserve it.</p>
<p>It can take a lot of time to feel safe again after abuse or sexual assault.</p>
<p> Talking to a therapist or counselor who’s trained to work with sexual assault, abuse and rape survivors can really help.</p>
<p> Connecting with other people who have been through the same thing, like a support group or with people you already know.</p>
<p>You’re not alone. Unfortunately, many people experience sexual abuse, assault, or rape at some point in their lives. You can get through this — and there are people who can help.</p>
</li>
</ol><br><br>