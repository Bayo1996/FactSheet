<h4><b>objective:</b></h4>
<p>To empower service providers with knowledge to assist the youth to develop life skills that will help them face the challenges of growing up and make decisions about their future.</p>
<h4><b>Definition of life skills</b></h4>
<p>Life skills are abilities or psychological competences that help the individual to deal or cope effectively with the challenges of every day life.They help young people identify goals and build good and healthy futures.</p>
<p>The acquired abilities are practiced, mastered and perfected to become skills.</p>

<p>There are three categories of life skills</p>
<ol><b>
<li> Skills for dealing and coping with oneself</li>
<li> Skills for dealing and coping with others</li>
<li> Skills for making effective decisions</li></b>
</ol>

<h4><b>Skills for dealing and coping with oneself</b></h4>
<ul>
<li>Self awareness</li>
<li>Self esteem</li>
<li>Coping with emotions</li>
<li>Coping with stress</li>
<li>Goal setting</li>
<li>Time management</li>
</ul>
<h4><b>Skills for dealing and coping with others</b></h4>
<ul>
<li>Interpersonal relationships</li>
<li>Friendship formation</li>
<li>Conflict resolution</li>
<li>Empathy</li>
<li>Effective communication</li>
<li>Resisting peer pressure </li>
<li>assertiveness</li>
<li>Negotiation</li>
</ul>
<h4><b>Skills for making effective decisions</b></h4>
<ul>
<li>Critical thinking</li>
<li>Creative thinking</li>
<li>Decision making</li>
<li>Problem solving</li>
<li>Employment economically</li>
<li>Socially and Education</li>
</ul>
<h4><b>Self-awareness</b></h4>
<p>This is the ability to understand who you are as well as your strengths and weakness. It enables one to work on his /her weakness and build on his/her strengths. It helps people to accept corrections.</p>
<h4><b>Critical thinking</b></h4>
<p>This is the ability to analyze a situation and give your own opinion even if it differs from others. It helps young people make their own personal decisions</p>
<h4><b>Self esteem</b></h4>
<ul>
<li>Self-esteem is a word used to describe how people feel about themselves. Influences their actions towards others and what they can accomplish in life.</li>
<li>Someone with high self-esteem accepts his or her mistakes and moves on.They feel confident about their own decisions.</li>
<li>If another person tries to convince or persuade them to do something they do not want to do, people who
feel good about themselves will be less likely to fall under that person’s pressure.</li>
<li>People are not born with self-esteem. It is learned as children realize that they are loved and valued. As children hear positive remarks including praise, encouragement, and reassurance, about themselves and the things they do, their selfesteem is strengthened.</li>
</ul>

<h4><b>Ways to improve self-esteem for young people</b></h4>
<ul>
<li>Not comparing them with others.</li>
<li>Recognizing their special talents and appreciating themselves the way they are.</li>
<li>Making a list of their best qualities.</li>
<li>Being aware of things they would like to improve about themselves, but not being overly self-critical.</li>
 <li>Setting realistic and achievable goals.</li>
<li>Believing in themselves.</li>
<li>Spending time with people who care for them and make them feel good about themselves.</li>
<li>Staying away from people who damage their self-esteem.</li>
</ul>
<h4><b>Factors that lower self-esteem</b></h4>
<ol>
<li>young people may have low selfesteem because often, the “negative” things about people are highlighted while the <b>“positive”</b> things are not spoken about.
<li><b>Put-down:</b> A word, phrase, expression, statement, gesture, or situation that results in a person not feeling good enough, or important, or capable, or less valued or less significant than before. Examples include:
<b>“You fool!” ,“You are a cow!”</b><li>
</ol>
<h4><b>Some ways we might put ourselves down include:</b></h4>
<ul>
 <li>Not accepting compliments.</li>
<li>Giving credit to others when it belongs to us.</li>
<li>Frequently giving others opinions before our own.</li>
 <li>Accepting and responding to nicknames like <b>“fatty.”</b></li>
</ul>
 <h4><b>people who are used to being put down:</b></h4>
 <ul>
<li>Find it difficult to socialize or meet new people because they are afraid of rejection</li>
<li>Are easily influenced or do things they do not want to do.</li>
<li>Cannot stand up for their rights.</li>
<li>Are shy and withdrawn.</li>
<li>Lack confidence.</li>
<li>Find it difficult to make decisions.</li>
</ul>
<h3><b>Assertiveness</b></h3>
<p>Being assertive means standing up for yourself and being honest with yourself and others about what you need and want. Being assertive can help young people protect themselves from dangerous situations and can help them resist peer pressure.People who are not assertive often lack the confidence and self esteem to stand up for their own needs and to protect their feelings or body from being hurt.Assertiveness is very different from being aggressive.People who are aggressive are rude and unkind. They do not care about other people’s feelings.</p>

<h4><b>Golden Rules of Assertiveness</b></h4>
<ul>
<li>Decide what you feel or want and say it.</li>
<li>Maintain eye contact.</li>
<li>Do not make excuses.Your feelings are the best reasons.</li>
<li>Show people that you know your own mind and are not looking for their approval.</li>
<li>Do not get confused by the other person’s argument. Keep repeating what you want or do not want.</li>
<li>Remember you have a right to change your mind.</li>
</ul>
<h5><b>Passiveness</b></h5>
<ul>
<li>Giving in to the will of others.</li>
<li>Hoping to get what you want without having to say it.</li>
<li>Leaving it to others to guess or letting them decide for you.</li>
<li>Being submissive.</li>
<li>Talking quietly, giggling, looking down or away, sagging shoulders, hiding ones face.</li>
<li> Avoiding disagreement.</li>
<li>Submissive type</li>
</ul>
<h5><b>Assertiveness</b></h5>
<ul>
 <li>Telling someone exactly what you want in a way that does not seem rude or threatening.</li>
<li>Standing up for your rights without endangering the rights of others.</li>
<li>Knowing what you need and want.</li>
<li>Expressing yourself with “I” statements say<b> “I feel” not “You…”</b></li>
<li>Looking the other in the eye.</li>
 <li>Standing your ground.</li>
<li><b>Balanced type</b></li>
</ul>

<h5><b>Aggressiveness</b></h5>
<ul>
<li>Expressing your feelings or desires in a way that threatens
or punishes others.</li>
<li>Insisting on your rights while denying their rights.</li>
<li>Dominating, shouting, demanding, not listening to others.</li>
<li>Looking down on people.</li>
<li>Saying others are wrong.</li>
<li>Blaming, threatening, or fighting with others.</li>
<li>Dominant type</li>
</ul>
<h4><b>Decision Making</b></h4>
<ul>
<li>A decision is a choice that we make between two or more possible courses of action. As young people grow, they make decisions some of which will affect them for the rest of their lives.</li>
<li>One of the most important parts of decision making is predicting outcomes or understanding the consequences ofa choice.The better young people are at predicting outcomes, the better they will be at making decisions that result in the outcomes they want. Understanding the possible consequences of decisions help young people to cope with the choices they have to make about their lives.</li>
</ul>
<h4><b>Key steps for good decision-making include:</b></h4>
<ul>
<li>Identifying the problem, situation, or issue that is calling for a decision.</li>
<li>Thinking about the possible consequences or outcomes of each course of action.</li>
<li>Thinking about your personal and family values, and which courses of action are consistent with these values.</li>
<li>Thinking about the ways in which your decision may affect other people.</li>
<li>Choosing the decision that seems most appropriate based on your knowledge, values, morals, religious upbringing, and present and future goals.</li>
<li>Re-thinking the decision and how you feel about it – whether you feel that you carefully considered all the alternatives and feel comfortable with the choice you have made.</li>
</ul>

<h4><b>Another way of looking at decision making process is by using the acronym DECIDE.</b></h4>
<ul>
<li><b>D – Describe the problem in your own words</b></li>
<li><b>E – Explore the available options</b></li>
<li><b>C – Choose the option that best fits you</b></li>
<li><b>I – Identify the advantages and disadvantages of the option you have chosen</b></li>
<li><b>D – Do it if the advantages outweigh the disadvantages</b></li>
<li><b>E – Evaluate whether your option is working. If not go back to the first “E”</b></li>
</ul>

<h4><b>Three C’s for Decision Making</b></h4>
<ol>
<li><b>CHALLENGE</b> Describe the CHALLENGE (or decision) you have.</li>
<li><b>CHOICES</b>List at least three CHOICES you have.</li>
<li><b>CONSEQUENCES</b> Consider the positive and negative CONSEQUENCES of each choice.</li>
</ol>
<br>
<h4 align="center"><b>Goal setting</b></h4>
  <p> Goals give us something to look forward to. To set a goal, we must learn about what we want to achieve. Goals should be specific, practical, and have a deadline. Goals should be realistic, manageable and achievable. Achieving a goal makes it easier and creates confidence to move to other, greater goals. Thinking about the expected benefits can be motivating. To help achieve a goal, it is helpful to have a plan with steps that are needed to achieve the goal, and also think about possible difficulties and how they can be overcome.</p>


