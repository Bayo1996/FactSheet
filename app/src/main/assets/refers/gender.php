<p>There’s a lot more to being male, female, or any gender than the sex assigned at birth.</p> <p>Your biological or assigned sex does not always tell your complete story.</p>

<p><b>What are the differences between sex, gender, and gender identity?</b></p>

<p>It’s common for people to confuse sex, gender, and gender identity.  But they’re actually all different things.</p>
<ul>
<li><b>Sex</b> is a label — male or female — that you’re assigned by a doctor at birth based on the genitals you’re born with and the chromosomes you have. It goes on your birth certificate.</li>
<li><p><b>Gender is much more complex:</b> It’s a social and legal status, and set of expectations from society, about behaviors, characteristics, and thoughts.</p>
<p> Each culture has standards about the way that people should behave based on their gender.</p>
<p> This is also generally male or female. But instead of being about body parts, it’s more about how you’re expected to act, because of your sex.</p></li>
<li>	Gender identity is how you feel inside and how you express your gender through clothing, behavior, and personal appearance. It’s a feeling that begins very early in life.</li>
<p><b>What’s assigned sex (aka “biological sex”)?</b></p>
<li><p>This is the state of being either a male or a female, it’s more of a label that you’re given at birth based on medical factors, including your hormones, chromosomes, and genitals.</p>
<p> Most people are assigned male or female, and this is what’s put on their birth certificates.</p></li>
<li>	When someone’s sexual and reproductive anatomy doesn’t seem to fit the typical definitions of female or male, they may be described as intersex.</li>
<li>	Some people call the sex we’re assigned at birth “biological sex.” But this term doesn’t fully capture the complex biological, anatomical, and chromosomal variations that can occur. Having only two options (biological male or biological female) might not describe what’s going on inside a person’s body.</li>
<li>	Instead of saying “biological sex,” some people use the phrase “assigned male at birth” or “assigned female at birth.” This acknowledges that someone (often a doctor) is making a decision for someone else. The assignment of a biological sex may or may not align with what’s going on with a person’s body, how they feel, or how they identify. </li>
</ul>
 
<p><b>The factors that determine our assigned sex begin as early as fertilization.</b>
<ul>
<li>	Each sperm has either an X or a Y chromosome in it. All eggs have an X chromosome.</li>
<li>	When sperm fertilizes an egg, its X or Y chromosome combines with the X chromosome of the egg.</li>
<li>	A person with XX chromosomes usually has female sex and reproductive organs, and is therefore usually assigned biologically female.</li>
<li>	A person with XY chromosomes usually has male sex and reproductive organs, and is therefore usually assigned biologically male.</li>
</p></ul>

<p>Other arrangements of <b>chromosomes, hormones, and body parts</b> can happen, which results in someone being <a href='https://www.plannedparenthood.org/learn/sexual-orientation-gender/gender-gender-identity/whats-intersex' target='_blank'>intersex</a>.</p>

<p><b>What’s gender?</b></p>
<p>Gender is much bigger and more complicated than assigned sex.</p>
<P> Gender includes <a href='https://www.plannedparenthood.org/learn/sexual-orientation-gender/gender-gender-identity/what-are-gender-roles-and-stereotypes' target='_blank'>gender roles</a>, which are expectations society and people have about behaviors, thoughts, and characteristics that go along with a person’s assigned sex.</p>
<p>For example, ideas about how men and women are expected to behave, dress, and communicate all contribute to gender. Gender is also a social and legal status as girls and boys, men, and women.</p>
<p>It’s easy to confuse sex and gender. Just remember that biological or assigned sex is about biology, anatomy, and chromosomes. Gender is society’s set of expectations, standards, and characteristics about how men and women are supposed to act.</p>


<p><b>What’s gender identity?</b>
	<ul>
<li>Your gender identity is how you feel inside and how you express those feelings.  Clothing, appearance, and behaviors can all be ways to express your gender identity.</li>
<li>	Most people feel that they’re either male or female.  Some people feel like a masculine female, or a feminine male. Some people feel neither male nor female. These people may choose labels such as “genderqueer,” “gender variant,” or “gender fluid.”  Your feelings about your gender identity begin as early as age 2 or 3.</li>
<li><p>Some people’s assigned sex and gender identity are pretty much the same, or in line with each other.</p>
<p>These people are called cisgender. Other people feel that their assigned sex is of the other gender from their gender identity (i.e., assigned sex is female, but gender identity is male).</p>
<p> These people are called  transgender or trans. Not all <a href='https://www.plannedparenthood.org/learn/sexual-orientation-gender/trans-and-gender-nonconforming-identities' target='_blank'>transgender</a> people share the same exact identity.</p></li>
<li>	Consent means actively agreeing to be sexual with someone. Consent lets someone know that sex is wanted. Sexual activity without consent is rape or sexual assault.</li>

