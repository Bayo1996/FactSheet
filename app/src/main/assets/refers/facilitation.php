<P>The facilitator’s skills are key to the success of the session and its usefulness to a discussion group.  The facilitator should feel comfortable discussing sensitive issue related to sexuality and should be careful not to project values or place judgment on the thoughts, opinions and experiences of group participants.  Most importantly, the facilitator needs basic skills in good listening.  Effective listening skills show participants that the facilitator is interested, attentive and respectful.</P>
<P> There are several elements of good listening skills, including:</P>
<ul>
<li><b>Body language</b> – posture, facial expressions, nodding of the head and sounds that encourage a participant to continue speaking.</li>
 <li><b>Reflecting</b> – the facilitator can, in his or her own words, repeat back to the group the key point that a participant has made.</li>
 <li><b>Questioning </b>– if asked appropriately, questions can be used to encourage a participant to elaborate or expand upon what he or she has just said.</li>
 <li><b>Summarizing</b> – the facilitator can summarize key points made by participants and use this summary as a natural transition to another topic.</li>
</ul>
 
<h4><b>HELPING LEARNERS TO HELP THEMSELVES</b></h4>
 
<p>As a facilitator there are many ways you can help your learners.  <b>You can:</b></p>
 <ul>
<li><b>Share your knowledge. </b> To help themselves, learners need knowledge.  Many health problems can be prevented if people know how.  But remember you do not have to have all of the answers to help people. It is fine to admit when you do not know something.  The people you work with will be glad for your honesty.</li>
 
<li><b>Treat students with respect.</b>  Each learner should be treated as someone who is capable of understanding his or her own health problems and of making good decisions them.  Never blame the learners for their problem or for past decisions they have made.</li>
 
<li><b>Keep health problems private.</b>  Health problems should not be discussed where others can hear.  Never tell out a problem your learner has unless he gives you the OK.</li>
<li><b>Remember that listening is more important than giving advice. </b> Learners often need someone who will listen to their problems without judgment.  By listening, you let them know you care and that he or she is important.  And as they get a chance to talk they will find out that they have some of the answers to the problem.</li>

