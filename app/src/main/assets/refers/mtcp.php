<h4><b>What factors increase the chance of transmission from mother to baby?</b></h4>
<p><b>Transmission of any infectious matter (including HIV) depends upon three factors:</b></p>
<ol>
<li>How infectious it is</li>
<li>How efficient the route of transmission is</li>
<li>How vulnerable the potential 'recipient' is</li>
</ol>

<p><b>Factors which increase the likelihood of transmission of HIV from mother to baby include:</b></p>
<ul>
<li>A high viral load, especially during labour and delivery. A mother with high viral load is more likely to transmit HIV, and transmission is most likely to occur during labour and delivery.
 However, though transmission is rare in mothers with undetectable viral load, it does still occur.</li>
<li>A low CD4 count or CD4 percentage.</li>
<li>Advanced HIV disease or previous AIDS-defining illness.</li>
<li>Older age of mother.</li>
<li>Smoking during pregnancy.</li>
<li>Use of recreational or illegal drugs during pregnancy. </li>
<li>Frequent unprotected sex, especially with multiple partners, during pregnancy.</li>
<li>No HIV therapy during pregnancy or delivery.</li>
<li>Premature delivery.</li>
<li>Waters breaking more than four hours before delivery.</li>
<li>Prolonged or difficult labour.</li>
<li>Cervical or vaginal infection (e.g. bacterial vaginosis).</li>
<li>Breastfeeding or mixed feeding (breast or formula).</li>
<li>The mother having another illness, such as TB or malaria, can increase the chance of transmission by elevating viral load or by causing immune activation.</li>
</ul>

<h4><b>What is mother-to-child transmission of HIV?</b></h4>
<p>Mother-to-child transmission of HIV is the spread of HIV from an HIV-infected woman to her child during pregnancy, childbirth (also called labor and delivery), or breastfeeding (through breast milk). Mother-to-child transmission of HIV is also called perinatal transmission of HIV.</p>
<p>Mother-to-child transmission is the most common way that children become infected with HIV.</p>
<h4><b>Can mother-to-child transmission of HIV be prevented?</b></h4>
<p><b>Yes.</b> Because of the use of HIV medicines and other strategies, the risk of mother-to-child transmission can be lowered to 1% or less.</p>
<p> The risk of mother-to-child transmission of HIV is low when: </p>
<ul>
<li>HIV is detected as early as possible during pregnancy (or before a woman gets pregnant).</li>
<li>Women with HIV receive HIV medicine during pregnancy and childbirth and, in certain situations, have a scheduled cesarean delivery (sometimes called a C-section).</li>
<li>Babies born to women with HIV receive HIV medicines for 4 to 6 weeks after birth and are not breastfed.</li>
</ul>
<h4><b>Is HIV testing recommended for pregnant women?</b></h4>
<p><b>The Centers for Disease Control and Prevention (CDC)</b> recommends that all women who are pregnant or planning to become pregnant get tested for HIV as early as possible—before, if possible, and during every pregnancy. </p>
<p>Pregnant women with HIV receive HIV medicines to reduce the risk of mother-to-child transmission of HIV and to protect their own health.</p>
<p> HIV medicines are recommended for everyone infected with HIV. HIV medicines help people with HIV live longer, healthier lives and reduce the risk of transmission of HIV.</p>

<h4><b>How do HIV medicines prevent mother-to-child transmission of HIV?</b></h4>
<p>HIV medicines work by preventing HIV from multiplying, which reduces the amount of HIV in the body.</p>
<p> Having less HIV in the body reduces a woman's risk of passing HIV to her child during pregnancy and childbirth. Having less HIV in the body also protects the woman's health.</p>

<p>Some of the HIV medicine passes from the pregnant woman to her unborn baby across the placenta (also called the afterbirth).</p>
<p> This transfer of HIV medicine protects the baby from HIV infection, especially during a vaginal delivery when the baby passes through the birth canal and is exposed to any HIV in the mother’s blood or other fluids.</p>
<p> In some situations, a woman with HIV may have a cesarean delivery (sometimes called a <b>C-section</b>) to reduce the risk of mother-to-child transmission of HIV during delivery.</p>
<p>Babies born to women with HIV receive HIV medicine for 4 to 6 weeks after birth.</p>
<p> The HIV medicine reduces the risk of infection from any HIV that may have entered a baby’s body during childbirth.</p>

<h4><b>Are HIV medicines safe to use during pregnancy?</b></h4>
<p>Most HIV medicines are safe to use during pregnancy.</p>
<p> In general, HIV medicines don’t increase the risk of birth defects.</p>
<p> Health care providers talk with HIV-infected women about the benefits and risks of specific HIV medicines to help the women decide which HIV medicines to use during pregnancy.</p>

<h4><b>Are there other ways to prevent mother-to-child transmission of HIV?</b></h4>
<p>Because HIV can be transmitted in breast milk, HIV-infected women in the United States should not breastfeed their babies.</p>
<p> In the United States, baby formula is a safe and healthy alternative to breast milk.</p>
<p> 
There are reports of children becoming infected with HIV by eating food that was previously chewed by a person infected with HIV.</p>
<p> To be safe, babies should not be fed pre-chewed food.</p>
