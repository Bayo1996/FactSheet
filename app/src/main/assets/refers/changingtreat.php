<p>Talk to your doctor if you are concerned about any side-effects you are experiencing.</p>
<p>If you are having problems taking your HIV treatment, it’s important to be honest with your doctor.</p>
<p>If side-effects persist, or your HIV becomes drug resistant, <b>changing treatment</b> may be an option.</p>
<p>There are a number of reasons why you may need to change your HIV treatment.</p>
<ul>
<li>You might experience side-effects which become unmanageable</li><li> you may have trouble taking your treatment (called adherence )</li> <li> the HIV drugs you take may not keep your viral load down.</li> <li>interactions between HIV drugs and drugs you take for other conditions</li> <li> experience drug resistance</li></ul>

<h4><b>Changing treatment because of side-effects</b></h4>
<p>All drugs can cause side-effects and the drugs used in treating HIV are no exception.</p>  <p>Side-effects are most noticeable soon after you start treatment with a drug but some only develop when you’ve been taking a drug for months or even years.</p>
<p>Side-effects checker may help you with this.</p>
<p> You shouldn't feel that you have to cope with side-effects alone.</p>
<p>It is particularly important to talk to your doctor if you develop a rash or fever soon after taking certain drugs</p>

<h4><b>Changing treatment because of resistance</b></h4>
<p>The best thing you can do to help your HIV treatment to work is to take it as prescribed every day.</p>
<p>If you are having problems taking your treatment, it's important to be honest with your doctor about this.</p>
<p>The aim of HIV treatment is to have a viral load that is ‘undetectable’, usually defined as below 50 copies/ml.</p>
<p> Your viral load should fall to undetectable levels within three to six months of starting treatment.</p>
<p> If it doesn’t, your doctor will talk to you about your current treatment.</p>
<p>If you don’t take your treatment as prescribed, your viral load can increase to detectable levels and your HIV may become resistant to the anti-HIV drugs you are taking. You may need to change treatment.</p>
<p> Your doctor will discuss your treatment options with you.</p>

