<h4><b>Is there evidence for PrEP?</b> </h4>
<ul>
<li>  Large research studies, including some done in Kenya show that PrEP reduces new HIV infections by more than 90% if taken every day.  </li>
</ul> 
<h4><b>Evidence that PrEP works </b> </h4>
<ul>
<li>PrEP has been shown to work in rigorous clinical trials and also during demonstration ‘real world’ </li> </ul>
<h4><b>pilot projects</b> </h4>
<ul>
<li>However, the effectiveness of PrEP is dependent on adherence. 
Virtually no infections occurred in PrEP users who were adherence to the medication</li>
</ul>
<h4><b>Why PrEP?</b></h4>
<ul>
<li>
HIV is to be a big problem in Kenya, as we continue to have many new infections every year despite efforts at HIV prevention.  </li>
<li>More than 71,000 new infections occur annually.</li>
</ul>
 <h4><b>How does PrEP work?</b></h4>
 <ul>
 <li>If you are exposed to HIV, these medicines can stop the virus from multiplying and spreading throughout your body.  </li>
 <li>PrEP only works if you have enough medicine in your 
body, so you need to take PrEP every day.</li>
</ul>
<h4><b>Who is PrEP for? </b></h4>
<ul>
<li>PrEP is not for everyone, but may be a suitable prevention method for many people at risk of acquiring HIV.</li>
<li>Some of these people could include serodiscordant couples, and people who have sex with someone of unknown HIV status.</li>
<li>Remember that PrEP is for use only in HIV negative individuals</li>
</ul>
 <h4><b>Recommended ARVs for PrEP</b></h4>
 <ul>
 <li> In Kenya the recommended ARVs for use for pre-exposure prophylaxis are as follows </li>
 <h4><b>Recommended PrEP Regime </b></h4>
 </ul>
<ul>

<li>Preferred 	TDF 300mg/FTC 200 mg once daily </li>
<li>Alternative 1 	TDF 300 mg once daily </li>
<li>Alternative 2 	TDF 300/3TC 300 mg once daily </li>
<li>Follow-up and Monitoring of PrEP </li>	 
</ul>
 <h4><b>Reasons for scheduled monitoring of PrEP use</b></h4>
 <ul>
<li>Improving adherence </li>
<li>Routine HTS </li>
<li>Managing suspected acute HIV infection </li>
<li>Managing confirmed HIV infection </li>
<li>Managing adverse drug reactions</li>
<li>Minor </li>
<li>Elevated creatinine </li>
</ul>
<h4><b>Is PrEP safe? </b></h4>
<ul>
<li>Yes it is! </li>
<li>Although PrEP is safe, it will be provided under the supervision of trained health care workers.</li>
<li>Some people will experience some minor side effects, but this will resolve within 7-14 days.</li>
</ul>
<h5><b>Is PrEP safe? - 1</b> </h5>
<ul>
<li>Overall safety </li>
<li>TDF-based PrEP is generally safe and well tolerated. </li>
<li>Gastrointestinal symptoms are the most common.  </li>
<li>Occur in about 10% of individuals:  </li>
<li>Include nausea, diarrhoea, vomiting </li>
<li>Self-limited and subside with 1-2 weeks</li>
<li>Renal Effects </li>
<li>About 10% of PrEP users may experience mild, self-limited </li>
<li>A small proportion of users may experience progressive decline in renal function.</li>
<li>This very rare in users younger than 45 years of age without underlying kidney disease or risk of kidney disease</li>
</ul>
 <h5><b>Is PrEP safe? -2</b></h5>
 <ul>
<li> Bone mineral density adverse effect </li>
<li> A known side effect of TDF </li>
<li> In studies and pilot projects, mineral density changes were small (~1%); with no effect on health </li>
<li> Risk of fractures not higher in PrEP users compared to non-users </li>
<li>PrEP use in pregnancy and breastfeeding</li>
<li>	HIV negative partners of HIV+ partners can use PrEP if they wish to have a baby.</li> 
<li>PrEP does not interfere with female or male fertility </li>
<li>	PrEP can be used safely during pregnancy and breastfeeding.</li>
<li>Resistance occurs sometimes when antiretrovirals are used for treatment.</li>
<li>Extremely rare with PrEP, and limited to those with unrecognized acute HIV infection when starting PrEP </li>
<li>Resistance can only occur if there is infection.</li>
<li>Risk of resistance far outweighed by number of HIV infections averted by PrEP</li>
</ul>
<h4><b>Where can you get PrEP?</b></h4>
<ul>
<li>It will be available at designated places, including Public health facilities, Private health facilities, Pharmacies, and various community outlets</li>
</ul>
<h4><b>What PrEP Cannot do </b></h4>
<ul>
<li>Prevent other STIs </li>
<li>Prevent Pregnancy </li>
</ul>
