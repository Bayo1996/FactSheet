<h4><b>FAMILY PLANNING AND CONTRACEPTIVES</b></h4>
<p><b>STERILIZATION,</b> also called <b>“tubal ligation”</b> or <b>“hysteroscopic tubal sterilization”</b>> in women and <b>“vasectomy”</b> in men is an operation in which a health-care provider blocks or ties the fallopian tubes of a woman or the vas deferens of a man, so that eggs and sperm can’t travel to meet one another. These methods are meant to be permanent.</p> 
<li><b>Correct Use:</b> These procedures must be performed by a <b>health-care provider</b>.  The health care provider will inform the woman or man when and how to determine whether the procedure was effective.</li> 
<li><b>Effectiveness:</b> The procedures are highly effective; less than one percent of partners will experience a pregnancy during the first year of use.</li> 
<li><b>STI Prevention: Sterilization does not protect against STIs. </b>
