<h4><b>Functions of the male reproductive system structures</b></h4>
<ol>
<li><b>Penile shaft</b> – <p>is the organ for sexual intercourse and for passing urine.</p></li> 
<li><b>Testis</b> – <p>the male sexual gland. It produces sperms and testosterone hormone.</p></li>
<li><b>Prostate and seminal vesicle</b> – <p>produces seminal fluid, which is necessary for sperm mobility.</p></li>
<li><b>Urethral duct</b> – <p>the canal that ensures passage of sperms and urine.</p></li>
<li><b>Vas deferens</b>- <p>Referred to as the spermatic cord or epididymis.</p>
<p> It is a channel for sperms from the testis to the urethra.</p><p> Sperm maturation takes place in this channel. This is the duct that is severed during vasectomy.</p></li> 
<li><b>Cowper’s gland</b>  <p>Referred to as bulbo-urethral gland. It produces a fluid, which protects the sperm during transportation by neutralizing the acidity in the urethra/Scrotum</p></li>
</ol>

