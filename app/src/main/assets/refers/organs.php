<h4><b>Functional anatomy of the female reproductive organs</b></h4>
<ol>
<li><b>Mons pubis</b> – <p>is the pad of fat, which is covered by hair at the onset of puberty. Normal pubic hair in the female is distributed in an inverted triangle at the mons pubis.</p></li> 
<li><b>Labia majora</b> – <p>these are two large folds of skin which cover the external genitalia.</p></li> 
<li><b>Labia minora</b> – <p>these are two smaller folds of skin located beneath the labia majora.</p> <p>As they join in the upper part they form the hood of the clitoris.</p> <p>During sexual arousal, they secrete a thick white substance, which assist in sexual intercourse.</p> </li>
<li><b>Clitoris</b> – <p>it is an erectile tissue that corresponds to the male penis.</p> <p>Its only known role is erotic.</p> <p>It is always excised during female circumcision (Clitoridectomy)</p></li>
 <li><b>Vestibule</b> – <p>is the area between the two labia minora where the vagina, urethral orifice, and the skenes glands are located.</p></li>

<li><b>Vagina</b> – <p>the vagina is the canal that connects the uterus to the external genitalia.</p> <p>It’s main function is to allow sexual activity and vaginal birth.</p> <p>Its wall is made of muscle and mucosal layer. The muscles are arranged in a circular manner, which allows the vaginal walls to expand and contract.</p> <p>The rugae in the mucosal layer are important for sensation during sexual intercourse.</p></li> 
<li><b>Uterus</b> - <p>This includes the uterine body and the cervix. The function of the uterus is to accommodate the pregnancy.</p> <p>The activity of the ovarian hormones on the uterus leads to the formation of menstrual blood</p></li>
<li><b>Cervix</b> – <p>This is the lower part of the uterus that opens into the vagina.</p>  <p>Capacitiation of the sperm takes place in the cervix. This enables the sperm to travel up the uterus and to fertilize the ova.</p>
<p>The cervix also produces mucus, which closes the cervix and prevents germs from entering the uterus.</p>
<p>The cervical mucus plug thickens when one is using contraceptives thus enhancing efficacyAt the onset of labour, the cervical mucus plug mixed with blood comes out as show.</p> 
<p>Cancer of the cervix is the commonest reproductive tract cancer in females in Kenya.</p></li>
<li><b>Ovaries</b> – <p>these are the female gonads that produce the ova and the female gonadal hormones (oestrogen and progesterone).</p> </li>
<li><b>Fallopian tubes;</b> – <p>They are the ducts in charge of collecting/carrying and feeding the ovum until its arrival in the uterus. Usually fertilization takes place here.</p>
</li></ol><br><br>



